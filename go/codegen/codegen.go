type intLimits struct {
	min string
	max string
}

func intTypeLimits(k cambium.IntKind) intLimits {
	switch k {
	case cambium.IntKindI8:
		return intLimits{min: "-128", max: "127"}
	case cambium.IntKindI16:
		return intLimits{min: "-32768", max: "32767"}
	case cambium.IntKindI32:
		return intLimits{min: "-2147483648", max: "2147483647"}
	case cambium.IntKindI64:
		return intLimits{min: "-9223372036854775808", max: "9223372036854775807"}
	case cambium.IntKindU8:
		return intLimits{min: "0", max: "255"}
	case cambium.IntKindU16:
		return intLimits{min: "0", max: "65535"}
	case cambium.IntKindU32:
		return intLimits{min: "0", max: "4294967295"}
	case cambium.IntKindU64:
		return intLimits{min: "0", max: "18446744073709551615"}
	default:
		return intLimits{min: "-9223372036854775808", max: "9223372036854775807"}
	}
}

func parseIntBound(s string, limits intLimits) int64 {
	if s == "min" {
		v, _ := strconv.ParseInt(limits.min, 10, 64)
		return v
	}
	if s == "max" {
		v, _ := strconv.ParseInt(limits.max, 10, 64)
		return v
	}
	v, _ := strconv.ParseInt(s, 10, 64)
	return v
}

func parseLengthBound(s string) int64 {
	if s == "min" {
		return 0
	}
	if s == "max" {
		return int64(^uint(0) >> 1)
	}
	v, _ := strconv.ParseInt(s, 10, 64)
	return v
}
