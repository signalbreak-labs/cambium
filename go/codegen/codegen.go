type identityrefMember struct {
	module    string
	name      string
	variant   string
	prefix    string
	namespace string
}

func (g *goEmitter) ensureIdentityref(name, yangName string, members []identityrefMember) {
	if g.identityrefTypes[name] {
		return
	}
	g.identityrefTypes[name] = true
	var defs, asName, asJSONName, fromName, xmlPrefixNS, xmlValue []string
	for i, m := range members {
		constName := name + m.variant
		if i == 0 {
			defs = append(defs, fmt.Sprintf("\t%s %s = iota", constName, name))
		} else {
			defs = append(defs, fmt.Sprintf("\t%s", constName))
		}
		asName = append(asName, fmt.Sprintf("\tcase %s:\n\t\treturn %q", constName, m.name))
		jsonName := m.name
		if m.module != g.moduleName {
			jsonName = m.module + ":" + m.name
		}
		asJSONName = append(asJSONName, fmt.Sprintf("\tcase %s:\n\t\treturn %q", constName, jsonName))
		if jsonName == m.name {
			fromName = append(fromName, fmt.Sprintf("\tcase %q:\n\t\treturn %s, true", m.name, constName))
		} else {
			fromName = append(fromName, fmt.Sprintf("\tcase %q:\n\t\treturn %s, true", jsonName, constName))
		}
		if m.module == g.moduleName {
			xmlPrefixNS = append(xmlPrefixNS, fmt.Sprintf("\tcase %s:\n\t\treturn \"\", \"\", false", constName))
			xmlValue = append(xmlValue, fmt.Sprintf("\tcase %s:\n\t\treturn cambiumXMLEscapeText(%q)", constName, m.name))
		} else {
			xmlPrefixNS = append(xmlPrefixNS, fmt.Sprintf("\tcase %s:\n\t\treturn %q, %q, true", constName, m.prefix, m.namespace))
			xmlValue = append(xmlValue, fmt.Sprintf("\tcase %s:\n\t\treturn %q + \":\" + cambiumXMLEscapeText(%q)", constName, m.prefix, m.name))
		}
	}
	helper := fmt.Sprintf("// %s is a generated YANG identityref type for %q.\ntype %s int\n\nconst (\n%s\n)\n\n", name, yangName, name, strings.Join(defs, "\n"))
	helper += fmt.Sprintf("// AsName returns the bare identity name.\nfunc (v %s) AsName() string {\n\tswitch v {\n%s\n\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n", name, strings.Join(asName, "\n"))
	helper += fmt.Sprintf("// AsJSONName returns the JSON_IETF identity name.\nfunc (v %s) AsJSONName() string {\n\tswitch v {\n%s\n\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n", name, strings.Join(asJSONName, "\n"))
	helper += fmt.Sprintf("// XMLPrefixNS returns the XML prefix and namespace for foreign identities.\nfunc (v %s) XMLPrefixNS() (string, string, bool) {\n\tswitch v {\n%s\n\tdefault:\n\t\treturn \"\", \"\", false\n\t}\n}\n\n", name, strings.Join(xmlPrefixNS, "\n"))
	helper += fmt.Sprintf("// XMLValue returns the XML identity value.\nfunc (v %s) XMLValue() string {\n\tswitch v {\n%s\n\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n", name, strings.Join(xmlValue, "\n"))
	helper += fmt.Sprintf("// String returns the bare identity name.\nfunc (v %s) String() string { return v.AsName() }\n\n", name)
	helper += fmt.Sprintf("// Parse%s parses a JSON_IETF identity name.\nfunc Parse%s(name string) (%s, bool) {\n\tswitch name {\n%s\n\tdefault:\n\t\treturn 0, false\n\t}\n}\n\n", name, name, name, strings.Join(fromName, "\n"))
	g.helpers["xmlEscape"] = true
	g.typeSnippets[name] = helper
}

func (g *goEmitter) ensureUnion(name, yangName string, members []cambium.TypeInfo, prefix, fieldIdent string) {
	if g.unionTypes[name] {
		return
	}
	g.unionTypes[name] = true
	g.helpers["xmlEscape"] = true
	g.helpers["jsonEscape"] = true

	used := make(map[string]bool)
	var typeDefs, variantMethods []string
	for _, member := range members {
		label := unionMemberLabel(member)
		variant := safeVariantIdent(label, used)
		variantType := name + variant
		payloadType := g.unionPayloadType(name, variant, member, prefix, fieldIdent)
		if unionMemberNeedsWrapper(member) {
			typeDefs = append(typeDefs, fmt.Sprintf("// %s is the %s member of %s.\ntype %s struct {\n\tValue %s\n}\n", variantType, variant, name, variantType, payloadType))
		} else {
			typeDefs = append(typeDefs, fmt.Sprintf("// %s is the %s member of %s.\ntype %s %s\n", variantType, variant, name, variantType, payloadType))
		}
		variantMethods = append(variantMethods, g.unionVariantMethods(name, variantType, payloadType, member))
	}
	if len(typeDefs) == 0 {
		typeDefs = append(typeDefs, fmt.Sprintf("// %sString is the fallback string member of %s.\ntype %sString string\n", name, name, name))
		variantMethods = append(variantMethods, g.unionVariantMethods(name, name+"String", "string", cambium.TypeInfo{}))
	}
	helper := fmt.Sprintf("// %s is a generated YANG union type for %q.\ntype %s interface {\n\tis%s()\n\tXMLValue() string\n\tXMLPrefixNS() (string, string, bool)\n\tJSONValue() string\n\tString() string\n\tvalidate(path string) error\n}\n\n", name, yangName, name, name)
	helper += strings.Join(typeDefs, "\n")
	helper += "\n" + strings.Join(variantMethods, "\n")
	g.typeSnippets[name] = helper
}

func (g *goEmitter) unionPayloadType(unionName, variant string, member cambium.TypeInfo, prefix, fieldIdent string) string {
	switch r := member.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.unionPayloadType(unionName, variant, *realtype, prefix, fieldIdent)
		}
		return "string"
	case cambium.ResolvedBoolean:
		return "bool"
	case cambium.ResolvedInt:
		if len(r.Range) > 0 {
			name := unionIntRangePayloadTypeName(unionName, variant)
			g.intRangeTypes[name] = intRangeInfo{kind: r.Kind, bounds: r.Range}
			return name
		}
		return goTypeForIntKind(r.Kind)
	case cambium.ResolvedDecimal64:
		g.emittedDecimal64 = true
		return "Decimal64"
	case cambium.ResolvedString:
		if len(r.Length) > 0 {
			name := unionStringLengthPayloadTypeName(unionName, variant)
			g.stringLengthTypes[name] = stringLengthInfo{bounds: r.Length}
			return name
		}
		return "string"
	case cambium.ResolvedBinary, cambium.ResolvedUnknown:
		return "string"
	case cambium.ResolvedInstanceIdentifier:
		g.emittedInstanceID = true
		g.helpers["xmlEscape"] = true
		g.helpers["jsonEscape"] = true
		return "InstanceIdentifier"
	case cambium.ResolvedEmpty:
		return "struct{}"
	case cambium.ResolvedEnumeration:
		enumName := unionName + variant + "Enum"
		g.ensureEnum(enumName, variant, r.Values())
		return enumName
	case cambium.ResolvedBits:
		bitsName := unionName + variant + "Bits"
		g.ensureBits(bitsName, variant, r.Values())
		return bitsName
	case cambium.ResolvedIdentityRef:
		members := collectIdentityrefMembers(r.Bases())
		if len(members) == 0 {
			return "string"
		}
		identName := unionName + variant + "Enum"
		g.ensureIdentityref(identName, variant, members)
		return identName
	case cambium.ResolvedUnion:
		nestedName := unionName + variant + "Union"
		g.ensureUnion(nestedName, variant, r.Members(), prefix, fieldIdent+variant)
		return nestedName
	default:
		return "string"
	}
}

func (g *goEmitter) unionVariantMethods(unionName, variantType, payloadType string, member cambium.TypeInfo) string {
	xmlExpr := ""
	xmlPrefixExpr := "return \"\", \"\", false"
	jsonExpr := ""
	stringExpr := ""
	validateExpr := "return nil"

	switch r := member.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.unionVariantMethods(unionName, variantType, payloadType, *realtype)
		}
		xmlExpr = "cambiumXMLEscapeText(string(v))"
		jsonExpr = "cambiumJSONEscape(string(v))"
		stringExpr = "string(v)"
		validateExpr = "if err := cambiumValidateStringValue(string(v)); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil"
	case cambium.ResolvedBoolean:
		g.helpers["strconv"] = true
		xmlExpr = "strconv.FormatBool(bool(v))"
		jsonExpr = xmlExpr
		stringExpr = xmlExpr
	case cambium.ResolvedInt:
		if len(r.Range) > 0 {
			xmlExpr = fmt.Sprintf("%s(v).String()", payloadType)
			if r.Kind == cambium.IntKindI64 || r.Kind == cambium.IntKindU64 {
				jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s(v).String())", payloadType)
			} else {
				jsonExpr = xmlExpr
			}
			stringExpr = xmlExpr
			validateExpr = fmt.Sprintf("if _, err := New%s(%s(v).Get()); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", payloadType, payloadType)
			break
		}
		g.helpers["strconv"] = true
		if isSignedIntKind(r.Kind) {
			xmlExpr = "strconv.FormatInt(int64(v), 10)"
		} else {
			xmlExpr = "strconv.FormatUint(uint64(v), 10)"
		}
		if r.Kind == cambium.IntKindI64 || r.Kind == cambium.IntKindU64 {
			jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s)", xmlExpr)
		} else {
			jsonExpr = xmlExpr
		}
		stringExpr = xmlExpr
	case cambium.ResolvedDecimal64:
		xmlExpr = "Decimal64(v).String()"
		jsonExpr = "cambiumJSONEscape(Decimal64(v).String())"
		stringExpr = xmlExpr
		fd, ranges := decimal64ValidationArgs(member)
		validateExpr = fmt.Sprintf("if err := cambiumValidateDecimal64Value(Decimal64(v), %d, %s); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", fd, ranges)
	case cambium.ResolvedString:
		patterns := stringPatternLiteral(member)
		if patterns != "nil" {
			g.helpers["patternValidate"] = true
		}
		if len(r.Length) > 0 {
			xmlExpr = fmt.Sprintf("cambiumXMLEscapeText(%s(v).String())", payloadType)
			jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s(v).String())", payloadType)
			stringExpr = fmt.Sprintf("%s(v).String()", payloadType)
			validateExpr = fmt.Sprintf("if _, err := New%s(%s(v).String()); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", payloadType, payloadType)
			if patterns != "nil" {
				validateExpr = fmt.Sprintf("if _, err := New%s(%s(v).String()); err != nil { return cambiumValidationError(path, err.Error()) }\n\tif err := cambiumValidateStringPatterns(%s(v).String(), %s); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", payloadType, payloadType, payloadType, patterns)
			}
			break
		}
		xmlExpr = "cambiumXMLEscapeText(string(v))"
		jsonExpr = "cambiumJSONEscape(string(v))"
		stringExpr = "string(v)"
		validateExpr = "if err := cambiumValidateStringValue(string(v)); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil"
		if patterns != "nil" {
			validateExpr = fmt.Sprintf("if err := cambiumValidateStringValue(string(v)); err != nil { return cambiumValidationError(path, err.Error()) }\n\tif err := cambiumValidateStringPatterns(string(v), %s); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", patterns)
		}
	case cambium.ResolvedEnumeration:
		xmlExpr = fmt.Sprintf("cambiumXMLEscapeText(%s(v).String())", payloadType)
		jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s(v).String())", payloadType)
		stringExpr = fmt.Sprintf("%s(v).String()", payloadType)
		validateExpr = fmt.Sprintf("if %s(v).AsName() == \"\" { return cambiumValidationError(path, \"invalid enum value\") }\n\treturn nil", payloadType)
	case cambium.ResolvedBits:
		xmlExpr = fmt.Sprintf("cambiumXMLEscapeText(%s(v).String())", payloadType)
		jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s(v).String())", payloadType)
		stringExpr = fmt.Sprintf("%s(v).String()", payloadType)
	case cambium.ResolvedBinary:
		g.helpers["binaryParse"] = true
		xmlExpr = "cambiumXMLEscapeText(string(v))"
		jsonExpr = "cambiumJSONEscape(string(v))"
		stringExpr = "string(v)"
		validateExpr = fmt.Sprintf("if err := cambiumValidateBinaryString(string(v), %s); err != nil { return cambiumValidationError(path, err.Error()) }\n\treturn nil", binaryLengthRangeLiteral(member))
	case cambium.ResolvedIdentityRef:
		xmlExpr = fmt.Sprintf("%s(v).XMLValue()", payloadType)
		xmlPrefixExpr = fmt.Sprintf("return %s(v).XMLPrefixNS()", payloadType)
		jsonExpr = fmt.Sprintf("cambiumJSONEscape(%s(v).AsJSONName())", payloadType)
		stringExpr = fmt.Sprintf("%s(v).String()", payloadType)
		validateExpr = fmt.Sprintf("if %s(v).AsJSONName() == \"\" { return cambiumValidationError(path, \"invalid identityref value\") }\n\treturn nil", payloadType)
	case cambium.ResolvedInstanceIdentifier:
		xmlExpr = "InstanceIdentifier(v).XMLValue()"
		xmlPrefixExpr = "return InstanceIdentifier(v).XMLPrefixNS()"
		jsonExpr = "InstanceIdentifier(v).JSONValue()"
		stringExpr = "InstanceIdentifier(v).String()"
		validateExpr = "return InstanceIdentifier(v).validate(path)"
	case cambium.ResolvedUnion:
		xmlExpr = "v.Value.XMLValue()"
		xmlPrefixExpr = "return v.Value.XMLPrefixNS()"
		jsonExpr = "v.Value.JSONValue()"
		stringExpr = "v.Value.String()"
		validateExpr = "if v.Value == nil { return cambiumValidationError(path, \"invalid union value\") }\n\treturn v.Value.validate(path)"
	case cambium.ResolvedEmpty:
		xmlExpr = "\"\""
		jsonExpr = "\"[null]\""
		stringExpr = "\"\""
	default:
		xmlExpr = "cambiumXMLEscapeText(string(v))"
		jsonExpr = "cambiumJSONEscape(string(v))"
		stringExpr = "string(v)"
	}

	return fmt.Sprintf("func (%s) is%s() {}\nfunc (v %s) XMLValue() string { return %s }\nfunc (v %s) XMLPrefixNS() (string, string, bool) { %s }\nfunc (v %s) JSONValue() string { return %s }\nfunc (v %s) String() string { return %s }\nfunc (v %s) validate(path string) error { %s }\n", variantType, unionName, variantType, xmlExpr, variantType, xmlPrefixExpr, variantType, jsonExpr, variantType, stringExpr, variantType, validateExpr)
}

func unionMemberNeedsWrapper(member cambium.TypeInfo) bool {
	switch r := member.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return unionMemberNeedsWrapper(*realtype)
		}
		return false
	case cambium.ResolvedUnion:
		return true
	default:
		return false
	}
}

func (g *goEmitter) emitCambiumStructInterface(out *strings.Builder) {
	out.WriteString("// WithDefaultsMode controls generated JSON_IETF default handling.\n")
	out.WriteString("type WithDefaultsMode int\n\n")
	out.WriteString("const (\n")
	out.WriteString("\tWithDefaultsExplicit WithDefaultsMode = iota\n")
	out.WriteString("\tWithDefaultsTrim\n")
	out.WriteString("\tWithDefaultsAll\n")
	out.WriteString("\tWithDefaultsAllTagged\n")
	out.WriteString(")\n\n")
	out.WriteString("// ValidationError reports the first generated YANG validation failure.\n")
	out.WriteString("type ValidationError struct {\n")
	out.WriteString("\tPath    string\n")
	out.WriteString("\tMessage string\n")
	out.WriteString("}\n\n")
	out.WriteString("func (e *ValidationError) Error() string {\n")
	out.WriteString("\tif e == nil { return \"\" }\n")
	out.WriteString("\tif e.Path == \"\" { return e.Message }\n")
	out.WriteString("\treturn e.Path + \": \" + e.Message\n")
	out.WriteString("}\n\n")
	out.WriteString("func cambiumValidationError(path, message string) error {\n")
	out.WriteString("\treturn &ValidationError{Path: path, Message: message}\n")
	out.WriteString("}\n\n")
	out.WriteString("func cambiumJoinPath(parent, child string) string {\n")
	out.WriteString("\tif child == \"\" { return parent }\n")
	out.WriteString("\tif parent == \"\" { return \"/\" + child }\n")
	out.WriteString("\treturn parent + \"/\" + child\n")
	out.WriteString("}\n\n")
	out.WriteString("// CambiumStruct is implemented by every generated YANG data struct.\n")
	out.WriteString("type CambiumStruct interface {\n")
	out.WriteString("\tToXML() string\n")
	out.WriteString("\tToJSONIETF() string\n")
	out.WriteString("\tToJSONIETFWithDefaults(WithDefaultsMode) string\n")
	out.WriteString("\tValidate() error\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitStructRec(node cambium.SchemaNodeRef, structName string, out *strings.Builder) {
	if !isStructKind(node.Kind()) {
		return
	}

	var ordered []cambium.SchemaNodeRef
	switch node.Kind() {
	case cambium.SchemaNodeKindAction, cambium.SchemaNodeKindRPC:
		ordered = g.operationPayloadChildren(node)
	case cambium.SchemaNodeKindNotification:
		ordered = g.orderedChildrenList(node.DataChildren(true))
	case cambium.SchemaNodeKindList:
		ordered = orderedListChildren(node.DataChildren(true), node.ListKeys())
		ordered = append(ordered, g.operationChildren(node)...)
	default:
		ordered = g.orderedChildrenList(node.DataChildren(true))
		ordered = append(ordered, g.operationChildren(node)...)
	}
	fields := g.collectFields(structName, ordered)

	g.emitStructDefinition(structName, fields, out)
	g.emitFieldOrder(structName, fields, out)
	g.emitStructSerializers(structName, fields, node.Module().Name(), node.Namespace(), out)
	g.emitValidationMethods(structName, fields, g.validationSourceChildren(node), "", out)
	g.emitCambiumStructImpl(structName, out)

	for _, f := range fields {
		if isStructKind(f.node.Kind()) {
			g.emitStructRec(f.node, fieldConcreteType(f), out)
		}
	}
}

func (g *goEmitter) emitStructDefinition(name string, fields []fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "// %s is a generated YANG data struct.\n", name)
	fmt.Fprintf(out, "type %s struct {\n", name)
	for _, f := range fields {
		doc := g.fieldDoc(f)
		if doc != "" {
			fmt.Fprintf(out, "\t// %s\n", doc)
		}
		fmt.Fprintf(out, "\t%s %s\n", f.ident, f.goType)
	}
	if hasMetadataTargets(fields) {
		g.emittedMetadata = true
		g.helpers["xmlEscape"] = true
		g.helpers["jsonEscape"] = true
		g.helpers["jsonIndent"] = true
		g.needsSort = true
		out.WriteString("\t// CambiumMetadata carries RFC 7952 instance metadata by child wire name.\n")
		out.WriteString("\tCambiumMetadata map[string][]MetadataAnnotation\n")
	}
	out.WriteString("}\n\n")
}

func hasMetadataTargets(fields []fieldInfo) bool {
	for _, f := range fields {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindLeafList:
			return true
		}
	}
	return false
}

func fieldsByNode(fields []fieldInfo) map[cambium.SchemaNodeRef]fieldInfo {
	byNode := make(map[cambium.SchemaNodeRef]fieldInfo, len(fields))
	for _, f := range fields {
		byNode[f.node] = f
	}
	return byNode
}

func (g *goEmitter) fieldDoc(f fieldInfo) string {
	if desc, ok := f.node.Description(); ok && desc != "" {
		// Use first line only to keep generated lines reasonable.
		if i := strings.IndexByte(desc, '\n'); i >= 0 {
			return strings.TrimSpace(desc[:i])
		}
		return strings.TrimSpace(desc)
	}
	return fmt.Sprintf("Generated YANG %s %q", kindName(f.node.Kind()), f.wire)
}

func (g *goEmitter) emitFieldOrder(name string, fields []fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "// %sFieldOrder lists child wire names in schema declaration\n", name)
	out.WriteString("// order (list keys first).\n")
	fmt.Fprintf(out, "var %sFieldOrder = []string{", name)
	for i, f := range fields {
		if i > 0 {
			out.WriteString(", ")
		}
		fmt.Fprintf(out, "%q", f.wire)
	}
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitStructSerializers(name string, fields []fieldInfo, currentModule, currentNS string, out *strings.Builder) {
	g.emitStructWriters(name, fields, currentModule, currentNS, out)

	// ToXML
	fmt.Fprintf(out, "func (n *%s) ToXML() string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	out.WriteString("\tn.writeXML(&b, 0)\n")
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")

	// ToJSONIETF
	fmt.Fprintf(out, "func (n *%s) ToJSONIETF() string {\n", name)
	out.WriteString("\treturn n.ToJSONIETFWithDefaults(WithDefaultsExplicit)\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (n *%s) ToJSONIETFWithDefaults(mode WithDefaultsMode) string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	out.WriteString("\tn.writeJSONWithDefaults(&b, 0, mode)\n")
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")

	g.emitStructParser(name, fields, currentModule, out)
}

func (g *goEmitter) emitStructParser(name string, fields []fieldInfo, currentModule string, out *strings.Builder) {
	g.helpers["jsonParse"] = true
	fmt.Fprintf(out, "func (n *%s) parseJSONIETF(obj map[string]any) error {\n", name)
	out.WriteString("\tif n == nil { return nil }\n")
	byNode := fieldsByNode(fields)
	g.emitJSONUnknownFieldCheck(fields, func(f fieldInfo) string {
		return jsonWireName(f, currentModule)
	}, out)
	for _, f := range fields {
		g.emitJSONParseField("n", jsonWireName(f, currentModule), f, byNode, out)
	}
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitStructWriters(name string, fields []fieldInfo, currentModule, currentNS string, out *strings.Builder) {
	// writeXML
	fmt.Fprintf(out, "func (n *%s) writeXML(b *strings.Builder, depth int) {\n", name)
	out.WriteString("\tindent := strings.Repeat(\" \", depth*2)\n")
	for _, f := range fields {
		g.emitFieldXML(f, currentNS, out)
	}
	out.WriteString("}\n\n")

	// writeJSON
	g.helpers["jsonIndent"] = true
	fmt.Fprintf(out, "func (n *%s) writeJSON(b *strings.Builder, depth int) {\n", name)
	out.WriteString("\tn.writeJSONWithDefaults(b, depth, WithDefaultsExplicit)\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (n *%s) writeJSONWithDefaults(b *strings.Builder, depth int, mode WithDefaultsMode) {\n", name)
	if len(fields) == 0 {
		out.WriteString("\tb.WriteString(\"{}\")\n")
	} else {
		out.WriteString("\tif !n.hasContentWithDefaults(mode) {\n")
		out.WriteString("\t\tb.WriteString(\"{}\")\n")
		out.WriteString("\t\treturn\n")
		out.WriteString("\t}\n")
		out.WriteString("\tb.WriteByte('{')\n")
		out.WriteString("\tfirst := true\n")
		byNode := fieldsByNode(fields)
		for _, f := range fields {
			g.emitFieldJSON(f, currentModule, byNode, out)
		}
		out.WriteString("\tcambiumJSONIndent(b, depth)\n")
		out.WriteString("\tb.WriteByte('}')\n")
	}
	out.WriteString("}\n\n")

	g.emitHasContent(name, fields, out)
	g.emitHasContentWithDefaults(name, fields, out)
}

func (g *goEmitter) emitHasContent(name string, fields []fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "func (n *%s) hasContent() bool {\n", name)
	mandatory := false
	var conds []string
	for _, f := range fields {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				conds = append(conds, fmt.Sprintf("n.%s != nil", f.ident))
			} else {
				mandatory = true
			}
		case cambium.SchemaNodeKindLeafList:
			if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				conds = append(conds, fmt.Sprintf("!n.%s.IsEmpty()", f.ident))
			} else {
				conds = append(conds, fmt.Sprintf("len(n.%s) > 0", f.ident))
			}
		case cambium.SchemaNodeKindAnyData:
			if f.optional {
				conds = append(conds, fmt.Sprintf("n.%s != nil", f.ident))
			} else {
				mandatory = true
			}
		case cambium.SchemaNodeKindContainer:
			if f.optional {
				conds = append(conds, fmt.Sprintf("n.%s != nil", f.ident))
			} else {
				conds = append(conds, fmt.Sprintf("n.%s.hasContent()", f.ident))
			}
		case cambium.SchemaNodeKindList:
			if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				conds = append(conds, fmt.Sprintf("!n.%s.IsEmpty()", f.ident))
			} else {
				conds = append(conds, fmt.Sprintf("len(n.%s) > 0", f.ident))
			}
		case cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
			conds = append(conds, fmt.Sprintf("n.%s != nil", f.ident))
		}
	}
	if mandatory {
		out.WriteString("\treturn true\n")
	} else if len(conds) == 0 {
		out.WriteString("\treturn false\n")
	} else {
		fmt.Fprintf(out, "\treturn %s\n", strings.Join(conds, " || "))
	}
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitHasContentWithDefaults(name string, fields []fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "func (n *%s) hasContentWithDefaults(mode WithDefaultsMode) bool {\n", name)
	out.WriteString("\tif mode == WithDefaultsTrim {\n")
	g.emitTrimHasContentChecks(fields, out)
	out.WriteString("\t\treturn false\n")
	out.WriteString("\t}\n")
	out.WriteString("\tif n.hasContent() { return true }\n")
	var conds []string
	seenConds := make(map[string]bool)
	addCond := func(cond string) {
		if seenConds[cond] {
			return
		}
		seenConds[cond] = true
		conds = append(conds, cond)
	}
	byNode := fieldsByNode(fields)
	for _, f := range fields {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if _, ok := f.node.DefaultValue(); ok {
				cond := "mode == WithDefaultsAll || mode == WithDefaultsAllTagged"
				if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
					cond = "(" + cond + ") && (" + guard + ")"
				}
				addCond(cond)
			}
		case cambium.SchemaNodeKindLeafList:
			if len(f.node.DefaultValues()) > 0 {
				cond := "mode == WithDefaultsAll || mode == WithDefaultsAllTagged"
				if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
					cond = "(" + cond + ") && (" + guard + ")"
				}
				addCond(cond)
			}
		case cambium.SchemaNodeKindContainer:
			if f.optional {
				addCond(fmt.Sprintf("n.%s != nil && n.%s.hasContentWithDefaults(mode)", f.ident, f.ident))
			} else {
				cond := fmt.Sprintf("n.%s.hasContentWithDefaults(mode)", f.ident)
				if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
					cond += " && (" + guard + ")"
				}
				addCond(cond)
			}
		case cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
			addCond(fmt.Sprintf("n.%s != nil && n.%s.hasContentWithDefaults(mode)", f.ident, f.ident))
		}
	}
	if len(conds) == 0 {
		out.WriteString("\treturn false\n")
	} else {
		fmt.Fprintf(out, "\treturn %s\n", strings.Join(conds, " || "))
	}
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitTrimHasContentChecks(fields []fieldInfo, out *strings.Builder) {
	out.WriteString("\t\ttrimHasContent := false\n")
	for _, f := range fields {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if defaultValue, ok := f.node.DefaultValue(); ok {
				if f.optional {
					valueRef := "(*n." + f.ident + ")"
					if f.isUnion {
						valueRef = "n." + f.ident
					}
					fmt.Fprintf(out, "\t\tif n.%s != nil && %s != %s { return true }\n", f.ident, g.jsonLiteralExpr(valueRef, f), g.defaultJSONLiteralExpr(f, defaultValue))
				} else {
					fmt.Fprintf(out, "\t\tif %s != %s { return true }\n", g.jsonLiteralExpr("n."+f.ident, f), g.defaultJSONLiteralExpr(f, defaultValue))
				}
			} else if f.optional {
				fmt.Fprintf(out, "\t\tif n.%s != nil { return true }\n", f.ident)
			} else {
				out.WriteString("\t\ttrimHasContent = true\n")
			}
		case cambium.SchemaNodeKindLeafList:
			defaults := f.node.DefaultValues()
			if len(defaults) > 0 {
				defaultVar := g.emitLeafListDefaultCheck("n", f, defaults, out, "\t\t")
				if strings.HasPrefix(f.goType, "UserOrderedVec[") {
					fmt.Fprintf(out, "\t\tif n.%s.Len() > 0 && !%s { return true }\n", f.ident, defaultVar)
				} else {
					fmt.Fprintf(out, "\t\tif len(n.%s) > 0 && !%s { return true }\n", f.ident, defaultVar)
				}
			} else if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				fmt.Fprintf(out, "\t\tif !n.%s.IsEmpty() { return true }\n", f.ident)
			} else {
				fmt.Fprintf(out, "\t\tif len(n.%s) > 0 { return true }\n", f.ident)
			}
		case cambium.SchemaNodeKindAnyData:
			if f.optional {
				fmt.Fprintf(out, "\t\tif n.%s != nil { return true }\n", f.ident)
			} else {
				out.WriteString("\t\ttrimHasContent = true\n")
			}
		case cambium.SchemaNodeKindContainer:
			if f.optional {
				fmt.Fprintf(out, "\t\tif n.%s != nil { return true }\n", f.ident)
			} else {
				fmt.Fprintf(out, "\t\tif n.%s.hasContentWithDefaults(mode) { return true }\n", f.ident)
			}
		case cambium.SchemaNodeKindList:
			if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				fmt.Fprintf(out, "\t\tif !n.%s.IsEmpty() { return true }\n", f.ident)
			} else {
				fmt.Fprintf(out, "\t\tif len(n.%s) > 0 { return true }\n", f.ident)
			}
		case cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
			fmt.Fprintf(out, "\t\tif n.%s != nil { return true }\n", f.ident)
		}
	}
	out.WriteString("\t\tif trimHasContent { return true }\n")
}

func (g *goEmitter) emitLeafListDefaultCheck(owner string, f fieldInfo, defaults []string, out *strings.Builder, indent string) string {
	defaults = g.orderedDefaultValues(defaults, f)
	defaultVar := "isDefault" + f.ident
	fmt.Fprintf(out, "%s%s := false\n", indent, defaultVar)
	if strings.HasPrefix(f.goType, "UserOrderedVec[") {
		fmt.Fprintf(out, "%sif %s.%s.Len() == %d {\n", indent, owner, f.ident, len(defaults))
		fmt.Fprintf(out, "%s\t%s = true\n", indent, defaultVar)
		for i, defaultValue := range defaults {
			valueVar := fmt.Sprintf("default%sItem%d", f.ident, i)
			okVar := fmt.Sprintf("default%sOK%d", f.ident, i)
			fmt.Fprintf(out, "%s\t%s, %s := %s.%s.Get(%d)\n", indent, valueVar, okVar, owner, f.ident, i)
			fmt.Fprintf(out, "%s\tif !%s || %s != %s { %s = false }\n", indent, okVar, g.jsonLiteralExpr(valueVar, f), g.defaultJSONLiteralExpr(f, defaultValue), defaultVar)
		}
		fmt.Fprintf(out, "%s}\n", indent)
		return defaultVar
	}

	fmt.Fprintf(out, "%sif len(%s.%s) == %d {\n", indent, owner, f.ident, len(defaults))
	itemsExpr := fieldItemsExpr(owner, f.ident, f)
	if g.shouldSortCollection(f) {
		g.needsSort = true
		itemsExpr = "defaultOrdered" + f.ident
		fmt.Fprintf(out, "%s\t%s := append([]%s(nil), %s.%s...)\n", indent, itemsExpr, fieldConcreteType(f), owner, f.ident)
		g.emitSortSlice(itemsExpr, f, out, indent+"\t")
	}
	fmt.Fprintf(out, "%s\t%s = true\n", indent, defaultVar)
	for i, defaultValue := range defaults {
		valueRef := fmt.Sprintf("%s[%d]", itemsExpr, i)
		fmt.Fprintf(out, "%s\tif %s != %s { %s = false }\n", indent, g.jsonLiteralExpr(valueRef, f), g.defaultJSONLiteralExpr(f, defaultValue), defaultVar)
	}
	fmt.Fprintf(out, "%s}\n", indent)
	return defaultVar
}

