func (g *goEmitter) ensureEnum(name, yangName string, values []cambium.EnumValue) {
	if g.enumTypes[name] {
		return
	}
	g.enumTypes[name] = true
	used := make(map[string]bool)
	var defs, asName, fromName []string
	for i, ev := range values {
		variant := safeVariantIdent(ev.Name(), used)
		constName := name + variant
		if i == 0 {
			defs = append(defs, fmt.Sprintf("\t%s %s = %d", constName, name, ev.Value()))
		} else {
			defs = append(defs, fmt.Sprintf("\t%s %s = %d", constName, name, ev.Value()))
		}
		asName = append(asName, fmt.Sprintf("\tcase %s:\n\t\treturn %q", constName, ev.Name()))
		fromName = append(fromName, fmt.Sprintf("\tcase %q:\n\t\treturn %s, true", ev.Name(), constName))
	}

	helper := fmt.Sprintf("// %s is a generated YANG enumeration type for %q.\ntype %s int64\n\nconst (\n%s\n)\n\n", name, yangName, name, strings.Join(defs, "\n"))
	helper += fmt.Sprintf("// AsName returns the canonical YANG enum name.\nfunc (v %s) AsName() string {\n\tswitch v {\n%s\n\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n", name, strings.Join(asName, "\n"))
	helper += fmt.Sprintf("// String returns the canonical YANG enum name.\nfunc (v %s) String() string { return v.AsName() }\n\n", name)
	helper += fmt.Sprintf("// Parse%s parses a YANG enum name.\nfunc Parse%s(name string) (%s, bool) {\n\tswitch name {\n%s\n\tdefault:\n\t\treturn 0, false\n\t}\n}\n\n", name, name, name, strings.Join(fromName, "\n"))
	g.typeSnippets[name] = helper
}

