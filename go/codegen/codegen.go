// Package codegen is the Go side of Cambium's YANG → typed-language generator.
//
// v2 consumes the rich goyang-grade Module/SchemaNodeRef API and emits typed
// structs with a per-struct field-order manifest plus self-contained native XML
// and JSON_IETF serializers. The generated walk follows the compiled YANG
// declaration order (list keys first), so output is byte-identical to libyang
// for the same data.
package codegen

import (
	"fmt"
	"go/format"
	"math/big"
	"sort"
	"strconv"
	"strings"

	"github.com/signalbreak-labs/cambium/go/cambium"
	"github.com/signalbreak-labs/cambium/go/internal/xsdregex"
)

// GenerateGo returns a deterministic, gofmt-stable Go source file for the
// implemented module named module.
func GenerateGo(ctx *cambium.Context, module string) (string, error) {
	mod, err := ctx.Schema(module)
	if err != nil {
		return "", err
	}
	gen := &goEmitter{
		module:       mod,
		moduleName:   mod.Name(),
		modulePascal: toPascalCase(mod.Name()),
		ns:           mod.Namespace(),
	}
	src := gen.emit()
	if gen.emitErr != nil {
		return "", gen.emitErr
	}
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return src, fmt.Errorf("generated source is not gofmt-clean: %w", err)
	}
	return string(formatted), nil
}

type goEmitter struct {
	module             cambium.Module
	moduleName         string
	modulePascal       string
	ns                 string
	helpers            map[string]bool
	typeSnippets       map[string]string
	intRangeTypes      map[string]intRangeInfo
	stringLengthTypes  map[string]stringLengthInfo
	emittedDecimal64   bool
	emittedUserOrdered bool
	enumTypes          map[string]bool
	bitsTypes          map[string]bool
	identityrefTypes   map[string]bool
	unionTypes         map[string]bool
	emittedInstanceID  bool
	needsSort          bool
	emittedAnyData     bool
	emittedMetadata    bool
	emitErr            error
}

type intRangeInfo struct {
	kind   cambium.IntKind
	bounds []cambium.RangeBound
}

type stringLengthInfo struct {
	bounds []cambium.RangeBound
}

// fieldInfo carries everything needed to emit one generated struct field and
// its serializers.
type fieldInfo struct {
	node          cambium.SchemaNodeRef
	ident         string // exported Go field identifier
	wire          string // YANG wire name
	moduleName    string // owner module for JSON_IETF namespace qualification
	namespace     string // owner XML namespace
	goType        string // Go type expression
	optional      bool   // true for optional leaves / presence containers
	jsonKind      string // "String" | "BareNumber" | "QuotedNumber" | "Bool" | "Empty" | "InstanceIdentifier"
	isNewtype     bool   // true for generated range/length/decimal64 wrapper types
	intSigned     bool   // true for signed integer native types
	isEnum        bool
	isBits        bool
	isIdentityref bool
	isUnion       bool
}

func (g *goEmitter) emit() string {
	g.helpers = make(map[string]bool)
	g.typeSnippets = make(map[string]string)
	g.intRangeTypes = make(map[string]intRangeInfo)
	g.stringLengthTypes = make(map[string]stringLengthInfo)
	g.enumTypes = make(map[string]bool)
	g.bitsTypes = make(map[string]bool)
	g.identityrefTypes = make(map[string]bool)
	g.unionTypes = make(map[string]bool)

	rootChildren := g.documentTopLevelChildren()

	var body strings.Builder
	rootFields := g.collectFields(g.modulePascal, rootChildren)
	for _, f := range rootFields {
		if isStructKind(f.node.Kind()) {
			g.emitStructRec(f.node, fieldConcreteType(f), &body)
		}
	}
	g.emitCambiumStructInterface(&body)
	g.emitRootStruct(rootFields, &body)
	g.emitOperationDocuments(&body)

	out := &strings.Builder{}
	fmt.Fprintf(out, "// Generated by cambium-codegen for module %q.\n// Do not edit by hand.\n\n", g.moduleName)
	out.WriteString("package generated\n\n")
	g.emitImports(out)
	g.emitHelpers(out)
	out.WriteString(body.String())
	return out.String()
}

func (g *goEmitter) documentTopLevelChildren() []cambium.SchemaNodeRef {
	children := g.orderedChildrenList(g.module.TopLevel())
	seen := map[string]bool{moduleIdentityKey(g.module): true}
	for _, imp := range g.module.Imports() {
		imported, ok := g.module.ResolvePrefix(imp.Prefix)
		if !ok {
			continue
		}
		key := moduleIdentityKey(imported)
		if seen[key] {
			continue
		}
		seen[key] = true
		children = append(children, g.orderedChildrenList(imported.TopLevel())...)
	}
	return children
}

func moduleIdentityKey(mod cambium.Module) string {
	revision, _ := mod.Revision()
	return mod.Name() + "\x00" + revision
}

func (g *goEmitter) emitImports(out *strings.Builder) {
	needStrings := true // strings.Builder, strings.Repeat
	needJSONParse := g.helpers["jsonParse"]
	needFmt := needJSONParse || g.helpers["xmlEscape"] || g.helpers["jsonEscape"] || g.helpers["binaryParse"] || g.helpers["patternValidate"] || len(g.intRangeTypes) > 0 || len(g.stringLengthTypes) > 0 || g.emittedDecimal64 || len(g.bitsTypes) > 0
	needStrconv := needJSONParse || g.helpers["strconv"] || len(g.intRangeTypes) > 0 || g.emittedDecimal64
	needMathBig := needJSONParse && g.emittedDecimal64
	needUTF8 := needJSONParse || len(g.stringLengthTypes) > 0

	imports := []string{}
	if needStrings {
		imports = append(imports, "\"strings\"")
	}
	if g.helpers["binaryParse"] {
		imports = append(imports, "\"encoding/base64\"")
	}
	if needJSONParse {
		imports = append(imports, "\"encoding/json\"")
		imports = append(imports, "\"io\"")
	}
	if needFmt {
		imports = append(imports, "\"fmt\"")
	}
	if g.helpers["patternValidate"] {
		imports = append(imports, "\"regexp\"")
	}
	if needMathBig {
		imports = append(imports, "\"math/big\"")
	}
	if needStrconv {
		imports = append(imports, "\"strconv\"")
	}
	if needUTF8 {
		imports = append(imports, "\"unicode/utf8\"")
	}
	if g.needsSort || g.helpers["jsonParse"] {
		imports = append(imports, "\"sort\"")
	}
	sort.Strings(imports)

	if len(imports) == 1 {
		fmt.Fprintf(out, "import %s\n\n", imports[0])
		return
	}
	out.WriteString("import (\n")
	for _, imp := range imports {
		fmt.Fprintf(out, "\t%s\n", imp)
	}
	out.WriteString(")\n\n")
}

func (g *goEmitter) emitHelpers(out *strings.Builder) {
	if g.helpers["jsonParse"] {
		g.emitJSONParseHelper(out)
	}
	if g.helpers["binaryParse"] {
		g.emitBinaryParseHelper(out)
	}
	if g.helpers["patternValidate"] {
		g.emitPatternValidateHelper(out)
	}
	if len(g.bitsTypes) > 0 {
		g.emitBitsParseHelper(out)
	}
	if g.helpers["xmlEscape"] {
		out.WriteString("// cambiumXMLEscapeText escapes &, <, and > for XML text content.\n")
		out.WriteString("func cambiumXMLEscapeText(s string) string {\n")
		out.WriteString("\treturn strings.NewReplacer(\"&\", \"&amp;\", \"<\", \"&lt;\", \">\", \"&gt;\").Replace(s)\n")
		out.WriteString("}\n\n")
		out.WriteString("// cambiumXMLEscapeAttr escapes text for a double-quoted XML attribute value.\n")
		out.WriteString("func cambiumXMLEscapeAttr(s string) string {\n")
		out.WriteString("\treturn strings.NewReplacer(\"&\", \"&amp;\", \"\\\"\", \"&quot;\", \"<\", \"&lt;\", \">\", \"&gt;\").Replace(s)\n")
		out.WriteString("}\n\n")
		out.WriteString("func cambiumValidateXMLPrefix(prefix string) error {\n")
		out.WriteString("\tif prefix == \"\" { return nil }\n")
		out.WriteString("\tif strings.EqualFold(prefix, \"xml\") || strings.EqualFold(prefix, \"xmlns\") {\n")
		out.WriteString("\t\treturn fmt.Errorf(\"invalid XML namespace prefix %q\", prefix)\n")
		out.WriteString("\t}\n")
		out.WriteString("\tfor i, r := range prefix {\n")
		out.WriteString("\t\tvalid := r == '_' || (r >= 'A' && r <= 'Z') || (r >= 'a' && r <= 'z')\n")
		out.WriteString("\t\tif i > 0 {\n")
		out.WriteString("\t\t\tvalid = valid || r == '-' || r == '.' || (r >= '0' && r <= '9')\n")
		out.WriteString("\t\t}\n")
		out.WriteString("\t\tif !valid { return fmt.Errorf(\"invalid XML namespace prefix %q\", prefix) }\n")
		out.WriteString("\t}\n")
		out.WriteString("\treturn nil\n")
		out.WriteString("}\n\n")
	}
	if g.helpers["jsonEscape"] {
		out.WriteString("// cambiumJSONEscape renders a string as a JSON string literal, including\n")
		out.WriteString("// the surrounding quotes. It matches libyang's json_print_string: named\n")
		out.WriteString("// escapes only for \", \\, \\r, and \\t; every other ASCII control is \\uXXXX\n")
		out.WriteString("// with uppercase hex; multi-byte UTF-8 passes through unchanged.\n")
		out.WriteString("func cambiumJSONEscape(s string) string {\n")
		out.WriteString("\tvar b strings.Builder\n")
		out.WriteString("\tb.WriteByte('\"')\n")
		out.WriteString("\tfor _, c := range s {\n")
		out.WriteString("\t\tswitch c {\n")
		out.WriteString("\t\tcase '\"':\n")
		out.WriteString("\t\t\tb.WriteString(`\\\"`)\n")
		out.WriteString("\t\tcase '\\\\':\n")
		out.WriteString("\t\t\tb.WriteString(`\\\\`)\n")
		out.WriteString("\t\tcase '\\r':\n")
		out.WriteString("\t\t\tb.WriteString(`\\r`)\n")
		out.WriteString("\t\tcase '\\t':\n")
		out.WriteString("\t\t\tb.WriteString(`\\t`)\n")
		out.WriteString("\t\tdefault:\n")
		out.WriteString("\t\t\tif c < 0x20 || c == 0x7f {\n")
		out.WriteString("\t\t\t\tb.WriteString(fmt.Sprintf(`\\u%04X`, c))\n")
		out.WriteString("\t\t\t} else {\n")
		out.WriteString("\t\t\t\tb.WriteRune(c)\n")
		out.WriteString("\t\t\t}\n")
		out.WriteString("\t\t}\n")
		out.WriteString("\t}\n")
		out.WriteString("\tb.WriteByte('\"')\n")
		out.WriteString("\treturn b.String()\n")
		out.WriteString("}\n\n")
	}
	if g.helpers["jsonIndent"] {
		out.WriteString("// cambiumJSONIndent writes a newline followed by depth two-space indents.\n")
		out.WriteString("func cambiumJSONIndent(b *strings.Builder, depth int) {\n")
		out.WriteString("\tb.WriteByte('\\n')\n")
		out.WriteString("\tfor i := 0; i < depth; i++ {\n")
		out.WriteString("\t\tb.WriteString(\"  \")\n")
		out.WriteString("\t}\n")
		out.WriteString("}\n\n")
	}
	if g.emittedDecimal64 {
		g.emitDecimal64Helper(out)
	}
	if g.emittedUserOrdered {
		g.emitUserOrderedVecHelper(out)
	}
	if g.emittedInstanceID {
		g.emitInstanceIdentifierHelper(out)
	}
	if g.emittedAnyData {
		g.emitAnyDataHelper(out)
	}
	if g.emittedMetadata {
		g.emitMetadataHelper(out)
	}
	typeKeys := make([]string, 0)
	for key := range g.typeSnippets {
		typeKeys = append(typeKeys, key)
	}
	sort.Strings(typeKeys)
	for _, key := range typeKeys {
		out.WriteString(g.typeSnippets[key])
	}
	intRangeKeys := make([]string, 0)
	for name := range g.intRangeTypes {
		intRangeKeys = append(intRangeKeys, name)
	}
	sort.Strings(intRangeKeys)
	for _, name := range intRangeKeys {
		g.emitIntRangeHelper(name, g.intRangeTypes[name], out)
	}
	stringLengthKeys := make([]string, 0)
	for name := range g.stringLengthTypes {
		stringLengthKeys = append(stringLengthKeys, name)
	}
	sort.Strings(stringLengthKeys)
	for _, name := range stringLengthKeys {
		g.emitStringLengthHelper(name, g.stringLengthTypes[name], out)
	}
}

