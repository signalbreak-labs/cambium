func isGoKeyword(name string) bool {
	switch name {
	case "break", "default", "func", "interface", "select", "case", "defer", "go",
		"map", "struct", "chan", "else", "goto", "package", "switch", "const",
		"fallthrough", "if", "range", "type", "continue", "for", "import",
		"return", "var":
		return true
	default:
		return false
	}
}

