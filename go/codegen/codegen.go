func collectIdentityrefMembers(bases []cambium.Identity) []identityrefMember {
	seen := make(map[string]bool)
	var out []identityrefMember
	add := func(id cambium.Identity) {
		mod := id.Module()
		key := mod.Name() + ":" + id.Name()
		if seen[key] {
			return
		}
		seen[key] = true
		out = append(out, identityrefMember{
			module:    mod.Name(),
			name:      id.Name(),
			prefix:    mod.Prefix(),
			namespace: mod.Namespace(),
		})
	}
	for _, base := range bases {
		add(base)
		for _, derived := range base.Derived() {
			add(derived)
		}
	}
	sort.Slice(out, func(i, j int) bool {
		return out[i].module+":"+out[i].name < out[j].module+":"+out[j].name
	})
	usedVariants := make(map[string]bool, len(out))
	for i := range out {
		out[i].variant = safeVariantIdent(out[i].name, usedVariants)
	}
	return out
}

