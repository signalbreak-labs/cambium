func nestedXMLNSAttr(f fieldInfo, currentNS string) string {
	if f.namespace == "" || f.namespace == currentNS {
		return ""
	}
	return staticXMLNSAttr(f.namespace)
}

func staticXMLNSAttr(namespace string) string {
	if namespace == "" {
		return ""
	}
	return generatedStringLiteralContent(` xmlns="` + xmlAttributeEscape(namespace) + `"`)
}

func xmlAttributeEscape(value string) string {
	return strings.NewReplacer("&", "&amp;", `"`, "&quot;", "<", "&lt;", ">", "&gt;").Replace(value)
}

func generatedStringLiteralContent(value string) string {
	quoted := strconv.Quote(value)
	return quoted[1 : len(quoted)-1]
}

func jsonWireName(f fieldInfo, currentModule string) string {
	if f.moduleName == "" || f.moduleName == currentModule {
		return f.wire
	}
	return f.moduleName + ":" + f.wire
}

func scalarBaseType(f fieldInfo) string {
	s := strings.TrimPrefix(f.goType, "*")
	s = strings.TrimPrefix(s, "[]")
	if strings.HasPrefix(s, "UserOrderedVec[") && strings.HasSuffix(s, "]") {
		s = strings.TrimSuffix(strings.TrimPrefix(s, "UserOrderedVec["), "]")
	}
	return s
}

func fieldConcreteType(f fieldInfo) string {
	return scalarBaseType(f)
}

func fieldItemsExpr(owner, ident string, f fieldInfo) string {
	if strings.HasPrefix(f.goType, "UserOrderedVec[") {
		return owner + "." + ident + ".items"
	}
	return owner + "." + ident
}

func (g *goEmitter) emitCollectionItems(owner string, f fieldInfo, out *strings.Builder, indent string) string {
	if !g.shouldSortCollection(f) {
		return fieldItemsExpr(owner, f.ident, f)
	}
	g.needsSort = true
	items := "ordered" + f.ident
	fmt.Fprintf(out, "%s%s := append([]%s(nil), %s...)\n", indent, items, fieldConcreteType(f), fieldItemsExpr(owner, f.ident, f))
	g.emitSortSlice(items, f, out, indent)
	return items
}

func (g *goEmitter) shouldSortCollection(f fieldInfo) bool {
	if strings.HasPrefix(f.goType, "UserOrderedVec[") {
		return false
	}
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeafList:
		return f.jsonKind != "Empty"
	case cambium.SchemaNodeKindList:
		return len(g.sortableListKeyFields(f)) > 0
	default:
		return false
	}
}

func (g *goEmitter) emitSortSlice(items string, f fieldInfo, out *strings.Builder, indent string) {
	fmt.Fprintf(out, "%ssort.Slice(%s, func(i, j int) bool {\n", indent, items)
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeafList:
		fmt.Fprintf(out, "%s\treturn %s\n", indent, scalarLessExpr(items+"[i]", items+"[j]", f))
	case cambium.SchemaNodeKindList:
		for _, key := range g.sortableListKeyFields(f) {
			left := fmt.Sprintf("%s[i].%s", items, key.ident)
			right := fmt.Sprintf("%s[j].%s", items, key.ident)
			fmt.Fprintf(out, "%s\tif %s {\n", indent, scalarLessExpr(left, right, key))
			fmt.Fprintf(out, "%s\t\treturn true\n", indent)
			fmt.Fprintf(out, "%s\t}\n", indent)
			fmt.Fprintf(out, "%s\tif %s {\n", indent, scalarLessExpr(right, left, key))
			fmt.Fprintf(out, "%s\t\treturn false\n", indent)
			fmt.Fprintf(out, "%s\t}\n", indent)
		}
		out.WriteString(indent + "\treturn false\n")
	default:
		out.WriteString(indent + "\treturn false\n")
	}
	fmt.Fprintf(out, "%s})\n", indent)
}

func (g *goEmitter) listKeyFields(f fieldInfo) []fieldInfo {
	keyNames := make([]string, 0)
	for key := range f.node.ListKeys().Iter() {
		keyNames = append(keyNames, key.Name())
	}
	if len(keyNames) == 0 {
		return nil
	}
	ordered := orderedListChildren(f.node.DataChildren(true), f.node.ListKeys())
	fields := g.collectFields(fieldConcreteType(f), ordered)
	byName := make(map[string]fieldInfo, len(fields))
	for _, field := range fields {
		byName[field.wire] = field
	}
	keys := make([]fieldInfo, 0, len(keyNames))
	for _, name := range keyNames {
		if field, ok := byName[name]; ok {
			keys = append(keys, field)
		}
	}
	return keys
}

func (g *goEmitter) sortableListKeyFields(f fieldInfo) []fieldInfo {
	keys := g.listKeyFields(f)
	if len(keys) == 0 {
		return nil
	}
	out := keys[:0]
	for _, key := range keys {
		if key.jsonKind != "Empty" {
			out = append(out, key)
		}
	}
	return out
}

func scalarLessExpr(left, right string, f fieldInfo) string {
	if f.isUnion {
		return fmt.Sprintf("%s.String() < %s.String()", left, right)
	}
	if f.isIdentityref {
		return fmt.Sprintf("%s.AsJSONName() < %s.AsJSONName()", left, right)
	}
	base := scalarBaseType(f)
	switch f.jsonKind {
	case "Bool":
		return fmt.Sprintf("!%s && %s", left, right)
	case "BareNumber", "QuotedNumber":
		if base == "Decimal64" {
			return fmt.Sprintf("%s.Raw() < %s.Raw()", left, right)
		}
		if f.isNewtype {
			return fmt.Sprintf("%s.Get() < %s.Get()", left, right)
		}
		return fmt.Sprintf("%s < %s", left, right)
	case "String", "InstanceIdentifier":
		if base == "string" {
			return fmt.Sprintf("%s < %s", left, right)
		}
		return fmt.Sprintf("%s.String() < %s.String()", left, right)
	default:
		return fmt.Sprintf("%s.String() < %s.String()", left, right)
	}
}

func (g *goEmitter) xmlValueExpr(valueRef string, f fieldInfo) string {
	base := scalarBaseType(f)
	if f.isUnion {
		return fmt.Sprintf("%s.XMLValue()", valueRef)
	}
	if f.isIdentityref {
		return fmt.Sprintf("%s.XMLValue()", valueRef)
	}
	switch f.jsonKind {
	case "InstanceIdentifier":
		return fmt.Sprintf("%s.XMLValue()", valueRef)
	case "String":
		if f.isEnum || f.isBits {
			return fmt.Sprintf("cambiumXMLEscapeText(%s.String())", valueRef)
		}
		if base == "string" {
			return fmt.Sprintf("cambiumXMLEscapeText(%s)", valueRef)
		}
		return fmt.Sprintf("cambiumXMLEscapeText(%s.String())", valueRef)
	case "Bool":
		g.helpers["strconv"] = true
		return fmt.Sprintf("strconv.FormatBool(%s)", valueRef)
	case "BareNumber", "QuotedNumber":
		if f.isNewtype || base == "Decimal64" {
			return fmt.Sprintf("%s.String()", valueRef)
		}
		g.helpers["strconv"] = true
		if f.intSigned {
			return fmt.Sprintf("strconv.FormatInt(int64(%s), 10)", valueRef)
		}
		return fmt.Sprintf("strconv.FormatUint(uint64(%s), 10)", valueRef)
	}
	return fmt.Sprintf("%s.String()", valueRef)
}

func (g *goEmitter) emitFieldJSON(f fieldInfo, currentModule string, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder) {
	wire := jsonWireName(f, currentModule)
	ident := f.ident

	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if f.jsonKind == "Empty" {
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", ident)
				out.WriteString("\t\tif !first { b.WriteByte(',') }\n")
				out.WriteString("\t\tfirst = false\n")
				out.WriteString("\t\tcambiumJSONIndent(b, depth+1)\n")
				fmt.Fprintf(out, "\t\tb.WriteString(\"\\\"%s\\\": [null]\")\n", wire)
				fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t}\n")
			} else {
				out.WriteString("\tif !first { b.WriteByte(',') }\n")
				out.WriteString("\tfirst = false\n")
				out.WriteString("\tcambiumJSONIndent(b, depth+1)\n")
				fmt.Fprintf(out, "\tb.WriteString(\"\\\"%s\\\": [null]\")\n", wire)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
			return
		}
		if f.optional {
			defaultValue, hasDefault := f.node.DefaultValue()
			valueRef := "(*n." + ident + ")"
			if f.isUnion {
				valueRef = "n." + ident
			}
			fmt.Fprintf(out, "\tif n.%s != nil {\n", ident)
			if hasDefault {
				fmt.Fprintf(out, "\t\tif mode != WithDefaultsTrim || %s != %s {\n", g.jsonLiteralExpr(valueRef, f), g.defaultJSONLiteralExpr(f, defaultValue))
				g.emitScalarJSON(wire, "b", "depth+1", valueRef, f, out)
				fmt.Fprintf(out, "\t\t\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t\t}\n")
			} else {
				g.emitScalarJSON(wire, "b", "depth+1", valueRef, f, out)
				fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
			out.WriteString("\t}\n")
			if hasDefault {
				defaultCond := "n." + ident + " == nil && (mode == WithDefaultsAll || mode == WithDefaultsAllTagged)"
				if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
					defaultCond += " && (" + guard + ")"
				}
				fmt.Fprintf(out, "\tif %s {\n", defaultCond)
				g.emitScalarJSONLiteral(wire, "b", "depth+1", g.defaultJSONLiteralExpr(f, defaultValue), out)
				out.WriteString("\t}\n")
			}
		} else {
			if defaultValue, hasDefault := f.node.DefaultValue(); hasDefault {
				fmt.Fprintf(out, "\tif mode != WithDefaultsTrim || %s != %s {\n", g.jsonLiteralExpr("n."+ident, f), g.defaultJSONLiteralExpr(f, defaultValue))
				g.emitScalarJSON(wire, "b", "depth+1", "n."+ident, f, out)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t}\n")
			} else {
				g.emitScalarJSON(wire, "b", "depth+1", "n."+ident, f, out)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
		}
	case cambium.SchemaNodeKindLeafList:
		defaults := f.node.DefaultValues()
		defaultVar := ""
		if len(defaults) > 0 {
			defaultVar = g.emitLeafListDefaultCheck("n", f, defaults, out, "\t")
		}
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\tif n.%s.Len() > 0 {\n", ident)
		} else {
			fmt.Fprintf(out, "\tif len(n.%s) > 0 {\n", ident)
		}
		if defaultVar != "" {
			fmt.Fprintf(out, "\t\tif mode != WithDefaultsTrim || !%s {\n", defaultVar)
		}
		itemsExpr := g.emitCollectionItems("n", f, out, "\t\t")
		g.emitLeafListJSONArray(wire, "b", "depth+1", "depth+2", itemsExpr, f, out)
		fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(b, depth+1, %q, n.CambiumMetadata[%q], &first)\n", wire, f.wire)
		if defaultVar != "" {
			out.WriteString("\t\t}\n")
		}
		out.WriteString("\t}\n")
		if len(defaults) > 0 {
			defaultCond := "mode == WithDefaultsAll || mode == WithDefaultsAllTagged"
			if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
				defaultCond = "(" + defaultCond + ") && (" + guard + ")"
			}
			if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				fmt.Fprintf(out, "\tif n.%s.Len() == 0 && %s {\n", ident, defaultCond)
			} else {
				fmt.Fprintf(out, "\tif len(n.%s) == 0 && %s {\n", ident, defaultCond)
			}
			g.emitDefaultLeafListJSONArray(wire, "b", "depth+1", "depth+2", defaults, f, out)
			out.WriteString("\t}\n")
		}
	case cambium.SchemaNodeKindAnyData:
		if f.optional {
			fmt.Fprintf(out, "\tif n.%s != nil {\n", ident)
			g.emitAnyDataJSON(wire, "b", "depth+1", "(*n."+ident+")", f, out)
			out.WriteString("\t}\n")
		} else {
			g.emitAnyDataJSON(wire, "b", "depth+1", "n."+ident, f, out)
		}
	case cambium.SchemaNodeKindContainer, cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
		if f.optional {
			fmt.Fprintf(out, "\tif n.%s != nil {\n", ident)
			out.WriteString("\t\tif !first { b.WriteByte(',') }\n")
			out.WriteString("\t\tfirst = false\n")
			out.WriteString("\t\tcambiumJSONIndent(b, depth+1)\n")
			fmt.Fprintf(out, "\t\tb.WriteString(\"\\\"%s\\\": \")\n", wire)
			fmt.Fprintf(out, "\t\tn.%s.writeJSONWithDefaults(b, depth+1, mode)\n", ident)
			out.WriteString("\t}\n")
		} else {
			contentCond := "n." + ident + ".hasContentWithDefaults(mode)"
			if guard := g.choiceDefaultEmissionGuard("n", f, byNode); guard != "" {
				contentCond += " && (" + guard + ")"
			}
			fmt.Fprintf(out, "\tif %s {\n", contentCond)
			out.WriteString("\t\tif !first { b.WriteByte(',') }\n")
			out.WriteString("\t\tfirst = false\n")
			out.WriteString("\t\tcambiumJSONIndent(b, depth+1)\n")
			fmt.Fprintf(out, "\t\tb.WriteString(\"\\\"%s\\\": \")\n", wire)
			fmt.Fprintf(out, "\t\tn.%s.writeJSONWithDefaults(b, depth+1, mode)\n", ident)
			out.WriteString("\t}\n")
		}
	case cambium.SchemaNodeKindList:
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\tif n.%s.Len() > 0 {\n", ident)
		} else {
			fmt.Fprintf(out, "\tif len(n.%s) > 0 {\n", ident)
		}
		itemsExpr := g.emitCollectionItems("n", f, out, "\t\t")
		out.WriteString("\t\tif !first { b.WriteByte(',') }\n")
		out.WriteString("\t\tfirst = false\n")
		out.WriteString("\t\tcambiumJSONIndent(b, depth+1)\n")
		fmt.Fprintf(out, "\t\tb.WriteString(\"\\\"%s\\\": [\")\n", wire)
		fmt.Fprintf(out, "\t\tfor i, entry := range %s {\n", itemsExpr)
		out.WriteString("\t\t\tif i > 0 { b.WriteByte(',') }\n")
		out.WriteString("\t\t\tcambiumJSONIndent(b, depth+2)\n")
		out.WriteString("\t\t\tentry.writeJSONWithDefaults(b, depth+2, mode)\n")
		out.WriteString("\t\t}\n")
		out.WriteString("\t\tcambiumJSONIndent(b, depth+1)\n")
		out.WriteString("\t\tb.WriteByte(']')\n")
		out.WriteString("\t}\n")
	}
}

func (g *goEmitter) emitScalarJSON(wire, writer, depthExpr, valueRef string, f fieldInfo, out *strings.Builder) {
	g.emitScalarJSONLiteral(wire, writer, depthExpr, g.jsonLiteralExpr(valueRef, f), out)
}

func (g *goEmitter) emitScalarJSONLiteral(wire, writer, depthExpr, literalExpr string, out *strings.Builder) {
	fmt.Fprintf(out, "\tif !first { %s.WriteByte(',') }\n", writer)
	out.WriteString("\tfirst = false\n")
	fmt.Fprintf(out, "\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t%s.WriteString(\"\\\"%s\\\": \" + %s)\n", writer, wire, literalExpr)
}

func (g *goEmitter) jsonLiteralExpr(valueRef string, f fieldInfo) string {
	switch f.jsonKind {
	case "QuotedNumber":
		return "\"\\\"\" + " + g.scalarValueExpr(valueRef, f) + " + \"\\\"\""
	case "Empty":
		return `"[null]"`
	default:
		return g.scalarValueExpr(valueRef, f)
	}
}

func (g *goEmitter) defaultJSONLiteralExpr(f fieldInfo, value string) string {
	if info, ok := f.node.LeafType(); ok {
		if literal, ok := g.typeDefaultJSONLiteralExpr(info, value); ok {
			return literal
		}
		g.recordEmitError(fmt.Errorf("invalid default %q for %s", value, f.node.Path()))
	}
	switch f.jsonKind {
	case "String", "InstanceIdentifier":
		return g.jsonStringLiteralExpr(value)
	case "BareNumber", "Bool":
		return strconv.Quote(value)
	case "QuotedNumber":
		return strconv.Quote(`"` + value + `"`)
	case "Empty":
		return `"[null]"`
	default:
		return g.jsonStringLiteralExpr(value)
	}
}

func (g *goEmitter) recordEmitError(err error) {
	if g.emitErr == nil {
		g.emitErr = err
	}
}

func (g *goEmitter) typeDefaultJSONLiteralExpr(info cambium.TypeInfo, value string) (string, bool) {
	switch r := info.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.typeDefaultJSONLiteralExpr(*realtype, value)
		}
		return g.jsonStringLiteralExpr(value), true
	case cambium.ResolvedUnion:
		for _, member := range r.Members() {
			if literal, ok := g.typeDefaultJSONLiteralExpr(member, value); ok {
				return literal, true
			}
		}
		return "", false
	case cambium.ResolvedBoolean:
		if value == "true" || value == "false" {
			return strconv.Quote(value), true
		}
		return "", false
	case cambium.ResolvedInt:
		normalized, ok := integerDefaultJSONValue(value, r)
		if !ok {
			return "", false
		}
		if jsonIntegerKindQuoted(r.Kind) {
			return strconv.Quote(`"` + normalized + `"`), true
		}
		return strconv.Quote(normalized), true
	case cambium.ResolvedDecimal64:
		canonical, ok := decimal64DefaultCanonical(value, r)
		if !ok {
			return "", false
		}
		return strconv.Quote(`"` + canonical + `"`), true
	case cambium.ResolvedString:
		if !stringDefaultMatches(value, r) {
			return "", false
		}
		return g.jsonStringLiteralExpr(value), true
	case cambium.ResolvedBinary:
		if !binaryDefaultMatches(value, r) {
			return "", false
		}
		return g.jsonStringLiteralExpr(value), true
	case cambium.ResolvedEnumeration:
		if !enumDefaultMatches(value, r.Values()) {
			return "", false
		}
		return g.jsonStringLiteralExpr(value), true
	case cambium.ResolvedBits:
		canonical, ok := bitsDefaultCanonical(value, r.Values())
		if !ok {
			return "", false
		}
		return g.jsonStringLiteralExpr(canonical), true
	case cambium.ResolvedIdentityRef:
		jsonName, ok := g.identityrefDefaultJSONName(value, r)
		if !ok {
			return "", false
		}
		return g.jsonStringLiteralExpr(jsonName), true
	case cambium.ResolvedInstanceIdentifier, cambium.ResolvedUnknown:
		return g.jsonStringLiteralExpr(value), true
	default:
		return "", false
	}
}

func (g *goEmitter) jsonStringLiteralExpr(value string) string {
	g.helpers["jsonEscape"] = true
	return "cambiumJSONEscape(" + strconv.Quote(value) + ")"
}

func integerDefaultJSONValue(value string, resolved cambium.ResolvedInt) (string, bool) {
	limits := intTypeLimits(resolved.Kind)
	var normalized string
	var numeric *big.Int
	if isSignedIntKind(resolved.Kind) {
		parsed, err := strconv.ParseInt(value, 10, intKindBitSize(resolved.Kind))
		if err != nil {
			return "", false
		}
		normalized = strconv.FormatInt(parsed, 10)
		numeric = big.NewInt(parsed)
	} else {
		parsed, err := strconv.ParseUint(value, 10, intKindBitSize(resolved.Kind))
		if err != nil {
			return "", false
		}
		normalized = strconv.FormatUint(parsed, 10)
		numeric = new(big.Int).SetUint64(parsed)
	}
	if !integerDefaultInRange(numeric, resolved.Range, limits) {
		return "", false
	}
	return normalized, true
}

func integerDefaultInRange(value *big.Int, bounds []cambium.RangeBound, limits intLimits) bool {
	if len(bounds) == 0 {
		return true
	}
	for _, bound := range bounds {
		lo, ok := intBoundBig(bound.Min(), limits)
		if !ok {
			return false
		}
		hi, ok := intBoundBig(bound.Max(), limits)
		if !ok {
			return false
		}
		if value.Cmp(lo) >= 0 && value.Cmp(hi) <= 0 {
			return true
		}
	}
	return false
}

func intBoundBig(bound string, limits intLimits) (*big.Int, bool) {
	switch bound {
	case "min":
		bound = limits.min
	case "max":
		bound = limits.max
	}
	value := new(big.Int)
	if _, ok := value.SetString(bound, 10); !ok {
		return nil, false
	}
	return value, true
}

func decimal64DefaultCanonical(value string, resolved cambium.ResolvedDecimal64) (string, bool) {
	fd := resolved.FractionDigits().Value()
	raw, ok := decimal64DefaultRaw(value, fd)
	if !ok {
		return "", false
	}
	if len(resolved.Range) > 0 {
		inRange := false
		for _, bound := range resolved.Range {
			lo := parseDecimal64BoundRaw(bound.Min(), fd)
			hi := parseDecimal64BoundRaw(bound.Max(), fd)
			if raw >= lo && raw <= hi {
				inRange = true
				break
			}
		}
		if !inRange {
			return "", false
		}
	}
	return formatDecimal64Raw(raw, fd), true
}

func decimal64DefaultRaw(value string, fractionDigits uint8) (int64, bool) {
	if value == "" {
		return 0, false
	}
	negative := strings.HasPrefix(value, "-")
	text := strings.TrimPrefix(value, "-")
	text = strings.TrimPrefix(text, "+")
	if text == "" {
		return 0, false
	}
	wholeText, fracText, hasFraction := strings.Cut(text, ".")
	if wholeText == "" {
		return 0, false
	}
	if hasFraction && fracText == "" {
		return 0, false
	}
	if len(fracText) > int(fractionDigits) {
		return 0, false
	}
	if !decimalDigitsOnly(wholeText) || (fracText != "" && !decimalDigitsOnly(fracText)) {
		return 0, false
	}
	for len(fracText) < int(fractionDigits) {
		fracText += "0"
	}
	whole := new(big.Int)
	whole.SetString(wholeText, 10)
	scale := new(big.Int).Exp(big.NewInt(10), big.NewInt(int64(fractionDigits)), nil)
	raw := new(big.Int).Mul(whole, scale)
	if fracText != "" {
		frac := new(big.Int)
		frac.SetString(fracText, 10)
		raw.Add(raw, frac)
	}
	if negative {
		raw.Neg(raw)
	}
	if !raw.IsInt64() {
		return 0, false
	}
	return raw.Int64(), true
}

func decimalDigitsOnly(value string) bool {
	for _, c := range value {
		if c < '0' || c > '9' {
			return false
		}
	}
	return value != ""
}

func formatDecimal64Raw(raw int64, fractionDigits uint8) string {
	if fractionDigits == 0 {
		return strconv.FormatInt(raw, 10)
	}
	divisor := int64(1)
	for i := uint8(0); i < fractionDigits; i++ {
		divisor *= 10
	}
	whole := raw / divisor
	frac := raw % divisor
	if frac < 0 {
		frac = -frac
	}
	padded := strconv.FormatInt(frac, 10)
	width := int(fractionDigits)
	if len(padded) < width {
		padded = strings.Repeat("0", width-len(padded)) + padded
	} else if len(padded) > width {
		padded = padded[:width]
	}
	trimmed := strings.TrimRight(padded, "0")
	if trimmed == "" {
		trimmed = "0"
	}
	if raw < 0 {
		return fmt.Sprintf("-%d.%s", -whole, trimmed)
	}
	return fmt.Sprintf("%d.%s", whole, trimmed)
}

func stringDefaultMatches(value string, resolved cambium.ResolvedString) bool {
	length := int64(len([]rune(value)))
	if !lengthDefaultMatches(length, resolved.Length) {
		return false
	}
	for _, pattern := range resolved.Patterns {
		matched, err := regexp.MatchString("^(?:"+xsdregex.NativePattern(pattern.Regex())+")$", value)
		if err != nil {
			return false
		}
		if pattern.IsInverted() {
			matched = !matched
		}
		if !matched {
			return false
		}
	}
	return true
}

func binaryDefaultMatches(value string, resolved cambium.ResolvedBinary) bool {
	decoded, err := base64.StdEncoding.DecodeString(value)
	if err != nil {
		return false
	}
	return lengthDefaultMatches(int64(len(decoded)), resolved.Length)
}

func lengthDefaultMatches(length int64, bounds []cambium.RangeBound) bool {
	if len(bounds) == 0 {
		return true
	}
	for _, bound := range bounds {
		lo := parseLengthBound(bound.Min())
		hi := parseLengthBound(bound.Max())
		if length >= lo && length <= hi {
			return true
		}
	}
	return false
}

func enumDefaultMatches(value string, values []cambium.EnumValue) bool {
	for _, enum := range values {
		if enum.Name() == value {
			return true
		}
	}
	return false
}

func bitsDefaultCanonical(value string, values []cambium.EnumValue) (string, bool) {
	allowed := make(map[string]bool, len(values))
	for _, bit := range values {
		allowed[bit.Name()] = true
	}
	seen := make(map[string]bool)
	for _, token := range bitsASCIIFields(value) {
		if !allowed[token] || seen[token] {
			return "", false
		}
		seen[token] = true
	}
	canonical := make([]string, 0, len(seen))
	for _, bit := range values {
		if seen[bit.Name()] {
			canonical = append(canonical, bit.Name())
		}
	}
	return strings.Join(canonical, " "), true
}

func bitsASCIIFields(value string) []string {
	var names []string
	start := 0
	for start < len(value) {
		for start < len(value) && value[start] == ' ' {
			start++
		}
		end := start
		for end < len(value) && value[end] != ' ' {
			end++
		}
		if end > start {
			names = append(names, value[start:end])
		}
		start = end
	}
	return names
}

func (g *goEmitter) identityrefDefaultJSONName(value string, resolved cambium.ResolvedIdentityRef) (string, bool) {
	for _, member := range collectIdentityrefMembers(resolved.Bases()) {
		jsonName := member.name
		if member.module != g.moduleName {
			jsonName = member.module + ":" + member.name
		}
		prefixedName := ""
		if member.prefix != "" {
			prefixedName = member.prefix + ":" + member.name
		}
		if value == jsonName || value == prefixedName || (member.module == g.moduleName && value == member.name) {
			return jsonName, true
		}
	}
	return "", false
}

func (g *goEmitter) emitAnyDataJSON(wire, writer, depthExpr, valueRef string, f fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "\tif !first { %s.WriteByte(',') }\n", writer)
	out.WriteString("\tfirst = false\n")
	fmt.Fprintf(out, "\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t%s.WriteString(\"\\\"%s\\\": \")\n", writer, wire)
	fmt.Fprintf(out, "\tcambiumWriteRawJSON(%s, %s, %s.JSONValue())\n", writer, depthExpr, valueRef)
}

func (g *goEmitter) emitLeafListJSONArray(wire, writer, depthExpr, elemDepthExpr, itemsExpr string, f fieldInfo, out *strings.Builder) {
	fmt.Fprintf(out, "\t\tif !first { %s.WriteByte(',') }\n", writer)
	out.WriteString("\t\tfirst = false\n")
	fmt.Fprintf(out, "\t\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t\t%s.WriteString(\"\\\"%s\\\": [\")\n", writer, wire)
	fmt.Fprintf(out, "\t\tfor i, v := range %s {\n", itemsExpr)
	fmt.Fprintf(out, "\t\t\tif i > 0 { %s.WriteByte(',') }\n", writer)
	fmt.Fprintf(out, "\t\t\tcambiumJSONIndent(%s, %s)\n", writer, elemDepthExpr)
	g.emitJSONLeafListElement(writer, elemDepthExpr, "v", f, out)
	out.WriteString("\t\t}\n")
	fmt.Fprintf(out, "\t\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t\t%s.WriteByte(']')\n", writer)
}

func (g *goEmitter) emitDefaultLeafListJSONArray(wire, writer, depthExpr, elemDepthExpr string, defaults []string, f fieldInfo, out *strings.Builder) {
	defaults = g.orderedDefaultValues(defaults, f)
	fmt.Fprintf(out, "\t\tif !first { %s.WriteByte(',') }\n", writer)
	out.WriteString("\t\tfirst = false\n")
	fmt.Fprintf(out, "\t\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t\t%s.WriteString(\"\\\"%s\\\": [\")\n", writer, wire)
	for i, value := range defaults {
		if i > 0 {
			fmt.Fprintf(out, "\t\t%s.WriteByte(',')\n", writer)
		}
		fmt.Fprintf(out, "\t\tcambiumJSONIndent(%s, %s)\n", writer, elemDepthExpr)
		fmt.Fprintf(out, "\t\t%s.WriteString(%s)\n", writer, g.defaultJSONLiteralExpr(f, value))
	}
	fmt.Fprintf(out, "\t\tcambiumJSONIndent(%s, %s)\n", writer, depthExpr)
	fmt.Fprintf(out, "\t\t%s.WriteByte(']')\n", writer)
}

func (g *goEmitter) orderedDefaultValues(defaults []string, f fieldInfo) []string {
	out := append([]string(nil), defaults...)
	if len(out) < 2 || !g.shouldSortCollection(f) {
		return out
	}
	info, ok := f.node.LeafType()
	if !ok {
		sort.Strings(out)
		return out
	}
	sort.SliceStable(out, func(i, j int) bool {
		return g.defaultValueLess(info, out[i], out[j], f)
	})
	return out
}

func (g *goEmitter) defaultValueLess(info cambium.TypeInfo, left, right string, f fieldInfo) bool {
	if f.isUnion {
		leftText, leftOK := g.defaultTextForType(info, left)
		rightText, rightOK := g.defaultTextForType(info, right)
		if leftOK && rightOK {
			return leftText < rightText
		}
		return left < right
	}
	switch r := info.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.defaultValueLess(*realtype, left, right, f)
		}
	case cambium.ResolvedBoolean:
		if (left == "true" || left == "false") && (right == "true" || right == "false") {
			return left == "false" && right == "true"
		}
	case cambium.ResolvedInt:
		leftValue, leftOK := integerDefaultSortValue(left, r)
		rightValue, rightOK := integerDefaultSortValue(right, r)
		if leftOK && rightOK {
			return leftValue.Cmp(rightValue) < 0
		}
	case cambium.ResolvedDecimal64:
		fd := r.FractionDigits().Value()
		leftRaw, leftOK := decimal64DefaultRaw(left, fd)
		rightRaw, rightOK := decimal64DefaultRaw(right, fd)
		if leftOK && rightOK {
			return leftRaw < rightRaw
		}
	case cambium.ResolvedIdentityRef:
		leftText, leftOK := g.identityrefDefaultJSONName(left, r)
		rightText, rightOK := g.identityrefDefaultJSONName(right, r)
		if leftOK && rightOK {
			return leftText < rightText
		}
	}
	leftText, leftOK := g.defaultTextForType(info, left)
	rightText, rightOK := g.defaultTextForType(info, right)
	if leftOK && rightOK {
		return leftText < rightText
	}
	return left < right
}

func (g *goEmitter) defaultTextForType(info cambium.TypeInfo, value string) (string, bool) {
	switch r := info.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.defaultTextForType(*realtype, value)
		}
		return value, true
	case cambium.ResolvedUnion:
		for _, member := range r.Members() {
			if text, ok := g.defaultTextForType(member, value); ok {
				return text, true
			}
		}
		return "", false
	case cambium.ResolvedBoolean:
		if value == "true" || value == "false" {
			return value, true
		}
		return "", false
	case cambium.ResolvedInt:
		return integerDefaultJSONValue(value, r)
	case cambium.ResolvedDecimal64:
		return decimal64DefaultCanonical(value, r)
	case cambium.ResolvedString:
		if !stringDefaultMatches(value, r) {
			return "", false
		}
		return value, true
	case cambium.ResolvedBinary:
		if !binaryDefaultMatches(value, r) {
			return "", false
		}
		return value, true
	case cambium.ResolvedEnumeration:
		if enumDefaultMatches(value, r.Values()) {
			return value, true
		}
		return "", false
	case cambium.ResolvedBits:
		return bitsDefaultCanonical(value, r.Values())
	case cambium.ResolvedIdentityRef:
		return g.identityrefDefaultJSONName(value, r)
	case cambium.ResolvedInstanceIdentifier, cambium.ResolvedUnknown:
		return value, true
	default:
		return "", false
	}
}

func integerDefaultSortValue(value string, resolved cambium.ResolvedInt) (*big.Int, bool) {
	normalized, ok := integerDefaultJSONValue(value, resolved)
	if !ok {
		return nil, false
	}
	numeric := new(big.Int)
	if _, ok := numeric.SetString(normalized, 10); !ok {
		return nil, false
	}
	return numeric, true
}

func (g *goEmitter) emitJSONLeafListElement(writer, depthExpr, valueRef string, f fieldInfo, out *strings.Builder) {
	switch f.jsonKind {
	case "String", "InstanceIdentifier":
		g.helpers["jsonEscape"] = true
		expr := g.scalarValueExpr(valueRef, f)
		fmt.Fprintf(out, "\t\t%s.WriteString(%s)\n", writer, expr)
	case "BareNumber", "Bool":
		expr := g.scalarValueExpr(valueRef, f)
		fmt.Fprintf(out, "\t\t%s.WriteString(%s)\n", writer, expr)
	case "QuotedNumber":
		expr := g.scalarValueExpr(valueRef, f)
		fmt.Fprintf(out, "\t\t%s.WriteString(\"\\\"\" + %s + \"\\\"\")\n", writer, expr)
	}
}

func (g *goEmitter) scalarValueExpr(valueRef string, f fieldInfo) string {
	base := scalarBaseType(f)
	if f.isUnion {
		return fmt.Sprintf("%s.JSONValue()", valueRef)
	}
	if f.isIdentityref {
		return fmt.Sprintf("cambiumJSONEscape(%s.AsJSONName())", valueRef)
	}
	switch f.jsonKind {
	case "InstanceIdentifier":
		return fmt.Sprintf("%s.JSONValue()", valueRef)
	case "String":
		if f.isEnum || f.isBits {
			return fmt.Sprintf("cambiumJSONEscape(%s.String())", valueRef)
		}
		if base == "string" {
			return fmt.Sprintf("cambiumJSONEscape(%s)", valueRef)
		}
		return fmt.Sprintf("cambiumJSONEscape(%s.String())", valueRef)
	case "Bool":
		g.helpers["strconv"] = true
		return fmt.Sprintf("strconv.FormatBool(%s)", valueRef)
	case "BareNumber":
		if f.isNewtype {
			return fmt.Sprintf("%s.String()", valueRef)
		}
		g.helpers["strconv"] = true
		if f.intSigned {
			return fmt.Sprintf("strconv.FormatInt(int64(%s), 10)", valueRef)
		}
		return fmt.Sprintf("strconv.FormatUint(uint64(%s), 10)", valueRef)
	case "QuotedNumber":
		if f.isNewtype || base == "Decimal64" {
			return fmt.Sprintf("%s.String()", valueRef)
		}
		g.helpers["strconv"] = true
		if f.intSigned {
			return fmt.Sprintf("strconv.FormatInt(%s, 10)", valueRef)
		}
		return fmt.Sprintf("strconv.FormatUint(%s, 10)", valueRef)
	}
	return fmt.Sprintf("%s.String()", valueRef)
}

func (g *goEmitter) emitValidationMethods(name string, fields []fieldInfo, sourceChildren []cambium.SchemaNodeRef, rootPath string, out *strings.Builder) {
	fmt.Fprintf(out, "func (n *%s) Validate() error {\n", name)
	out.WriteString("\tif n == nil { return nil }\n")
	fmt.Fprintf(out, "\treturn n.validate(%q)\n", rootPath)
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (n *%s) validate(path string) error {\n", name)
	out.WriteString("\tif n == nil { return nil }\n")
	g.emitMetadataValidation(fields, out)
	g.emitChoiceValidation(fields, sourceChildren, out)
	for _, f := range fields {
		g.emitFieldValidation(f, out)
	}
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitMetadataValidation(fields []fieldInfo, out *strings.Builder) {
	targets := metadataTargetFields(fields)
	if len(targets) == 0 {
		return
	}
	out.WriteString("\tfor metadataNode := range n.CambiumMetadata {\n")
	out.WriteString("\t\tswitch metadataNode {\n")
	for _, f := range targets {
		fmt.Fprintf(out, "\t\tcase %q:\n", f.wire)
	}
	out.WriteString("\t\tdefault:\n")
	out.WriteString("\t\t\treturn cambiumValidationError(cambiumJoinPath(path, metadataNode), \"metadata for unknown data node\")\n")
	out.WriteString("\t\t}\n")
	out.WriteString("\t}\n")
	for _, f := range targets {
		fmt.Fprintf(out, "\tif len(n.CambiumMetadata[%q]) > 0 {\n", f.wire)
		fmt.Fprintf(out, "\t\tif !(%s) { return cambiumValidationError(cambiumJoinPath(path, %q), %q) }\n", fieldPresentExpr("n", f), f.wire, "metadata for absent data node")
		fmt.Fprintf(out, "\t\tfor _, item := range n.CambiumMetadata[%q] {\n", f.wire)
		fmt.Fprintf(out, "\t\t\tif err := cambiumValidateMetadataAnnotation(item); err != nil { return cambiumValidationError(cambiumJoinPath(path, %q), err.Error()) }\n", f.wire)
		out.WriteString("\t\t}\n")
		out.WriteString("\t}\n")
	}
}

func metadataTargetFields(fields []fieldInfo) []fieldInfo {
	var out []fieldInfo
	for _, f := range fields {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindLeafList:
			out = append(out, f)
		}
	}
	return out
}

func (g *goEmitter) emitStringValueValidationAt(f fieldInfo, owner, pathExpr string, out *strings.Builder, indent string) {
	if f.isUnion || !stringValueType(f.node) {
		return
	}
	base := fieldConcreteType(f)
	_, isLengthType := g.stringLengthTypes[base]
	ref := owner + "." + f.ident
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if f.optional {
			valueExpr := "*" + ref
			if isLengthType {
				valueExpr = ref + ".String()"
			}
			fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
			fmt.Fprintf(out, "%s\tif err := cambiumValidateStringValue(%s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
			return
		}
		valueExpr := ref
		if isLengthType {
			valueExpr = ref + ".String()"
		}
		fmt.Fprintf(out, "%sif err := cambiumValidateStringValue(%s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, pathExpr)
	case cambium.SchemaNodeKindLeafList:
		itemsExpr := fieldItemsExpr(owner, f.ident, f)
		valueExpr := "v"
		if isLengthType {
			valueExpr = "v.String()"
		}
		fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
		fmt.Fprintf(out, "%s\tif err := cambiumValidateStringValue(%s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, pathExpr)
		fmt.Fprintf(out, "%s}\n", indent)
	}
}

func (g *goEmitter) emitFieldValidation(f fieldInfo, out *strings.Builder) {
	wire := f.wire
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeafList, cambium.SchemaNodeKindList:
		lenExpr := validationLenExpr("n", f)
		if min, ok := f.node.MinElements(); ok && min > 0 {
			if !f.node.IsChoiceDescendant() {
				fmt.Fprintf(out, "\tif %s < %d {\n", lenExpr, min)
				fmt.Fprintf(out, "\t\treturn cambiumValidationError(cambiumJoinPath(path, %q), %q)\n", wire, "min-elements violation")
				out.WriteString("\t}\n")
			}
		}
		if max, ok := f.node.MaxElements(); ok {
			if !f.node.IsChoiceDescendant() {
				fmt.Fprintf(out, "\tif %s > %d {\n", lenExpr, max)
				fmt.Fprintf(out, "\t\treturn cambiumValidationError(cambiumJoinPath(path, %q), %q)\n", wire, "max-elements violation")
				out.WriteString("\t}\n")
			}
		}
	}

	base := fieldConcreteType(f)
	if _, ok := g.intRangeTypes[base]; ok {
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif _, err := New%s(n.%s.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, f.ident, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif _, err := New%s(n.%s.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, f.ident, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif _, err := New%s(v.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, pathExpr)
			out.WriteString("\t}\n")
		}
	}
	if _, ok := g.stringLengthTypes[base]; ok {
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif _, err := New%s(n.%s.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, f.ident, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif _, err := New%s(n.%s.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, f.ident, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif _, err := New%s(v.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", base, pathExpr)
			out.WriteString("\t}\n")
		}
	}

	g.emitStringValueValidationAt(f, "n", fmt.Sprintf("cambiumJoinPath(path, %q)", wire), out, "\t")

	if info, ok := stringPatternInfo(f.node); ok {
		g.helpers["patternValidate"] = true
		patterns := stringPatternLiteral(info)
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		_, isLengthType := g.stringLengthTypes[base]
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				valueExpr := fmt.Sprintf("*n.%s", f.ident)
				if isLengthType {
					valueExpr = fmt.Sprintf("n.%s.String()", f.ident)
				}
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", valueExpr, patterns, pathExpr)
				out.WriteString("\t}\n")
			} else {
				valueExpr := fmt.Sprintf("n.%s", f.ident)
				if isLengthType {
					valueExpr = fmt.Sprintf("n.%s.String()", f.ident)
				}
				fmt.Fprintf(out, "\tif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", valueExpr, patterns, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			valueExpr := "v"
			if isLengthType {
				valueExpr = "v.String()"
			}
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", valueExpr, patterns, pathExpr)
			out.WriteString("\t}\n")
		}
	}

	if info, ok := binaryTypeInfo(f.node); ok {
		g.helpers["binaryParse"] = true
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		ranges := binaryLengthRangeLiteral(info)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif err := cambiumValidateBinaryString(*n.%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", f.ident, ranges, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif err := cambiumValidateBinaryString(n.%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", f.ident, ranges, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif err := cambiumValidateBinaryString(v, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", ranges, pathExpr)
			out.WriteString("\t}\n")
		}
	}

	if info, ok := decimal64TypeInfo(f.node); ok {
		fd, ranges := decimal64ValidationArgs(info)
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif err := cambiumValidateDecimal64Value(*n.%s, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", f.ident, fd, ranges, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif err := cambiumValidateDecimal64Value(n.%s, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", f.ident, fd, ranges, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif err := cambiumValidateDecimal64Value(v, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", fd, ranges, pathExpr)
			out.WriteString("\t}\n")
		}
	}

	if f.jsonKind == "InstanceIdentifier" {
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif err := n.%s.validate(%s); err != nil { return err }\n", f.ident, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif err := n.%s.validate(%s); err != nil { return err }\n", f.ident, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif err := v.validate(%s); err != nil { return err }\n", pathExpr)
			out.WriteString("\t}\n")
		}
	}

	if f.isEnum || f.isIdentityref {
		method := "AsName"
		message := "invalid enum value"
		if f.isIdentityref {
			method = "AsJSONName"
			message = "invalid identityref value"
		}
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil && n.%s.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", f.ident, f.ident, method, pathExpr, message)
			} else {
				fmt.Fprintf(out, "\tif n.%s.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", f.ident, method, pathExpr, message)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif v.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", method, pathExpr, message)
			out.WriteString("\t}\n")
		}
	}

	if f.isUnion {
		pathExpr := fmt.Sprintf("cambiumJoinPath(path, %q)", wire)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
				fmt.Fprintf(out, "\t\tif err := n.%s.validate(%s); err != nil { return err }\n", f.ident, pathExpr)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tif err := n.%s.validate(%s); err != nil { return err }\n", f.ident, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr("n", f.ident, f)
			fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
			fmt.Fprintf(out, "\t\tif err := v.validate(%s); err != nil { return err }\n", pathExpr)
			out.WriteString("\t}\n")
		}
	}

	if f.node.Kind() == cambium.SchemaNodeKindList {
		g.emitListKeyValidation(f, out)
		g.emitUniqueValidation(f, out)
		itemsExpr := fieldItemsExpr("n", f.ident, f)
		fmt.Fprintf(out, "\tfor i := range %s {\n", itemsExpr)
		fmt.Fprintf(out, "\t\tif err := %s[i].validate(cambiumJoinPath(path, %q)); err != nil { return err }\n", itemsExpr, wire)
		out.WriteString("\t}\n")
		return
	}
	if f.node.Kind() == cambium.SchemaNodeKindLeafList && requiresLeafListValueUniqueness(f.node) {
		g.emitLeafListUniqueValueValidation(f, out)
	}

	switch f.node.Kind() {
	case cambium.SchemaNodeKindContainer, cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
		if f.node.IsChoiceDescendant() {
			return
		}
		if f.optional {
			fmt.Fprintf(out, "\tif n.%s != nil {\n", f.ident)
			fmt.Fprintf(out, "\t\tif err := n.%s.validate(cambiumJoinPath(path, %q)); err != nil { return err }\n", f.ident, wire)
			out.WriteString("\t}\n")
		} else {
			fmt.Fprintf(out, "\tif err := n.%s.validate(cambiumJoinPath(path, %q)); err != nil { return err }\n", f.ident, wire)
		}
	}
}

func requiresLeafListValueUniqueness(node cambium.SchemaNodeRef) bool {
	return node.RepresentsConfigurationData() || node.YangVersion() != "1.1"
}

func (g *goEmitter) emitLeafListUniqueValueValidation(f fieldInfo, out *strings.Builder) {
	g.emitLeafListUniqueValueValidationAt(f, "n", fmt.Sprintf("cambiumJoinPath(path, %q)", f.wire), out, "\t")
}

func (g *goEmitter) emitLeafListUniqueValueValidationAt(f fieldInfo, owner, pathExpr string, out *strings.Builder, indent string) {
	itemsExpr := fieldItemsExpr(owner, f.ident, f)
	seenName := fmt.Sprintf("seen%sValues", f.ident)
	fmt.Fprintf(out, "%s%s := make(map[string]struct{})\n", indent, seenName)
	fmt.Fprintf(out, "%sfor _, value := range %s {\n", indent, itemsExpr)
	keyExpr := g.uniqueStringExpr("value", f)
	fmt.Fprintf(out, "%s\tkey := %s\n", indent, keyExpr)
	fmt.Fprintf(out, "%s\tif _, ok := %s[key]; ok {\n", indent, seenName)
	fmt.Fprintf(out, "%s\t\treturn cambiumValidationError(%s, %q)\n", indent, pathExpr, "duplicate leaf-list value")
	fmt.Fprintf(out, "%s\t}\n", indent)
	fmt.Fprintf(out, "%s\t%s[key] = struct{}{}\n", indent, seenName)
	fmt.Fprintf(out, "%s}\n", indent)
}

func (g *goEmitter) emitListKeyValidation(f fieldInfo, out *strings.Builder) {
	g.emitListKeyValidationAt(f, "n", fmt.Sprintf("cambiumJoinPath(path, %q)", f.wire), out, "\t")
}

func (g *goEmitter) emitListKeyValidationAt(f fieldInfo, owner, pathExpr string, out *strings.Builder, indent string) {
	keys := g.listKeyFields(f)
	if len(keys) == 0 {
		return
	}
	itemsExpr := fieldItemsExpr(owner, f.ident, f)
	seenName := fmt.Sprintf("seen%sKeys", f.ident)
	fmt.Fprintf(out, "%s%s := make(map[string]struct{})\n", indent, seenName)
	parts := make([]string, 0, len(keys))
	usesEntry := false
	for _, key := range keys {
		if key.jsonKind == "Empty" {
			parts = append(parts, `"[null]"`)
			continue
		}
		usesEntry = true
		parts = append(parts, g.jsonLiteralExpr(uniqueFieldValueExpr("entry", key), key))
	}
	if usesEntry {
		fmt.Fprintf(out, "%sfor _, entry := range %s {\n", indent, itemsExpr)
	} else {
		fmt.Fprintf(out, "%sfor range %s {\n", indent, itemsExpr)
	}
	keyExpr := parts[0]
	if len(parts) > 1 {
		keyExpr = "strings.Join([]string{" + strings.Join(parts, ", ") + `}, "\x00")`
	}
	fmt.Fprintf(out, "%s\tkey := %s\n", indent, keyExpr)
	fmt.Fprintf(out, "%s\tif _, ok := %s[key]; ok {\n", indent, seenName)
	fmt.Fprintf(out, "%s\t\treturn cambiumValidationError(%s, %q)\n", indent, pathExpr, "duplicate key violation")
	fmt.Fprintf(out, "%s\t}\n", indent)
	fmt.Fprintf(out, "%s\t%s[key] = struct{}{}\n", indent, seenName)
	fmt.Fprintf(out, "%s}\n", indent)
}

func (g *goEmitter) emitChoiceValidation(fields []fieldInfo, sourceChildren []cambium.SchemaNodeRef, out *strings.Builder) {
	choices := validationChoices(sourceChildren)
	if len(choices) == 0 {
		return
	}
	byNode := make(map[cambium.SchemaNodeRef]fieldInfo, len(fields))
	for _, f := range fields {
		byNode[f.node] = f
	}
	choiceCounter := 0
	for _, choice := range choices {
		g.emitChoiceValidationForChoice(choice, "n", []string{choice.Name()}, byNode, out, "\t", &choiceCounter)
	}
}

func (g *goEmitter) emitChoiceValidationForChoice(choice cambium.SchemaNodeRef, owner string, path []string, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder, indent string, choiceCounter *int) {
	countName := fmt.Sprintf("choiceCount%d", *choiceCounter)
	*choiceCounter++
	type caseCheck struct {
		node cambium.SchemaNodeRef
		expr string
	}
	var caseChecks []caseCheck
	fmt.Fprintf(out, "%s%s := 0\n", indent, countName)
	for c := range choice.Children().Iter() {
		if c.Kind() != cambium.SchemaNodeKindCase {
			continue
		}
		expr := g.choiceCasePresenceExpr(owner, c, byNode)
		caseChecks = append(caseChecks, caseCheck{node: c, expr: expr})
		fmt.Fprintf(out, "%sif %s { %s++ }\n", indent, expr, countName)
	}
	fmt.Fprintf(out, "%sif %s > 1 {\n", indent, countName)
	fmt.Fprintf(out, "%s\treturn cambiumValidationError(%s, %q)\n", indent, choiceCasePathExpr(path), "choice has multiple cases selected")
	fmt.Fprintf(out, "%s}\n", indent)
	if choice.IsMandatory() {
		fmt.Fprintf(out, "%sif %s == 0 {\n", indent, countName)
		fmt.Fprintf(out, "%s\treturn cambiumValidationError(%s, %q)\n", indent, choiceCasePathExpr(path), "mandatory choice has no selected case")
		fmt.Fprintf(out, "%s}\n", indent)
	}
	for _, check := range caseChecks {
		casePath := append(append([]string(nil), path...), check.node.Name())
		caseExpr := check.expr
		if defaultCase, ok := choice.DefaultValue(); ok && defaultCase == check.node.Name() {
			caseExpr = fmt.Sprintf("(%s || %s == 0)", check.expr, countName)
		}
		g.emitChoiceCaseMandatoryValidation(check.node, caseExpr, casePath, owner, byNode, out, indent, choiceCounter)
	}
}

func (g *goEmitter) emitChoiceCaseMandatoryValidation(c cambium.SchemaNodeRef, caseExpr string, path []string, owner string, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder, indent string, choiceCounter *int) {
	var checks strings.Builder
	g.emitSelectedCaseDescendantValidation(c.Children(), owner, path, byNode, &checks, indent+"\t", choiceCounter)
	if checks.Len() == 0 {
		return
	}
	fmt.Fprintf(out, "%sif %s {\n", indent, caseExpr)
	out.WriteString(checks.String())
	fmt.Fprintf(out, "%s}\n", indent)
}

func (g *goEmitter) emitSelectedCaseDescendantValidation(children cambium.SchemaChildren, owner string, path []string, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder, indent string, choiceCounter *int) {
	for child := range children.Iter() {
		if len(child.Whens()) > 0 {
			continue
		}
		f, hasField := byNode[child]
		switch child.Kind() {
		case cambium.SchemaNodeKindChoice:
			choicePath := append(append([]string(nil), path...), child.Name())
			g.emitChoiceValidationForChoice(child, owner, choicePath, byNode, out, indent, choiceCounter)
		case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindAnyData:
			if !hasField {
				continue
			}
			fieldPath := append(append([]string(nil), path...), f.wire)
			if child.Kind() == cambium.SchemaNodeKindLeaf {
				g.emitScalarFieldValidationAt(f, owner, choiceCasePathExpr(fieldPath), out, indent)
			}
			if !child.IsMandatory() {
				continue
			}
			missingExpr := mandatoryFieldMissingExpr(owner, f)
			if missingExpr == "" {
				continue
			}
			fmt.Fprintf(out, "%sif %s {\n", indent, missingExpr)
			fmt.Fprintf(out, "%s\treturn cambiumValidationError(%s, %q)\n", indent, choiceCasePathExpr(fieldPath), "missing mandatory field")
			fmt.Fprintf(out, "%s}\n", indent)
		case cambium.SchemaNodeKindLeafList, cambium.SchemaNodeKindList:
			if !hasField {
				continue
			}
			fieldPath := append(append([]string(nil), path...), f.wire)
			fieldPathExpr := choiceCasePathExpr(fieldPath)
			if min, ok := child.MinElements(); ok && min > 0 {
				fmt.Fprintf(out, "%sif %s < %d {\n", indent, validationLenExpr(owner, f), min)
				fmt.Fprintf(out, "%s\treturn cambiumValidationError(%s, %q)\n", indent, fieldPathExpr, "min-elements violation")
				fmt.Fprintf(out, "%s}\n", indent)
			}
			if max, ok := child.MaxElements(); ok {
				fmt.Fprintf(out, "%sif %s > %d {\n", indent, validationLenExpr(owner, f), max)
				fmt.Fprintf(out, "%s\treturn cambiumValidationError(%s, %q)\n", indent, fieldPathExpr, "max-elements violation")
				fmt.Fprintf(out, "%s}\n", indent)
			}
			if child.Kind() == cambium.SchemaNodeKindLeafList {
				g.emitScalarFieldValidationAt(f, owner, fieldPathExpr, out, indent)
				if requiresLeafListValueUniqueness(child) {
					g.emitLeafListUniqueValueValidationAt(f, owner, fieldPathExpr, out, indent)
				}
				continue
			}
			g.emitListKeyValidationAt(f, owner, fieldPathExpr, out, indent)
			g.emitUniqueValidationAt(f, owner, fieldPathExpr, out, indent)
			entryFields := g.collectFields(fieldConcreteType(f), orderedListChildren(child.DataChildren(true), child.ListKeys()))
			entryByNode := make(map[cambium.SchemaNodeRef]fieldInfo, len(entryFields))
			for _, ef := range entryFields {
				entryByNode[ef.node] = ef
			}
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor i := range %s {\n", indent, itemsExpr)
			entryOwner := fmt.Sprintf("%s[i]", itemsExpr)
			fmt.Fprintf(out, "%s\tif err := %s.validate(%s); err != nil { return err }\n", indent, entryOwner, fieldPathExpr)
			g.emitSelectedCaseDescendantValidation(child.Children(), entryOwner, fieldPath, entryByNode, out, indent+"\t", choiceCounter)
			fmt.Fprintf(out, "%s}\n", indent)
		case cambium.SchemaNodeKindContainer:
			if !hasField {
				continue
			}
			nextOwner := owner + "." + f.ident
			nextPath := append(append([]string(nil), path...), f.wire)
			nextPathExpr := choiceCasePathExpr(nextPath)
			nestedFields := g.collectFields(fieldConcreteType(f), g.orderedChildrenList(child.DataChildren(true)))
			nestedByNode := make(map[cambium.SchemaNodeRef]fieldInfo, len(nestedFields))
			for _, nf := range nestedFields {
				nestedByNode[nf.node] = nf
			}
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, nextOwner)
				fmt.Fprintf(out, "%s\tif err := %s.validate(%s); err != nil { return err }\n", indent, nextOwner, nextPathExpr)
				g.emitSelectedCaseDescendantValidation(child.Children(), nextOwner, nextPath, nestedByNode, out, indent+"\t", choiceCounter)
				fmt.Fprintf(out, "%s}\n", indent)
				continue
			}
			fmt.Fprintf(out, "%sif err := %s.validate(%s); err != nil { return err }\n", indent, nextOwner, nextPathExpr)
			g.emitSelectedCaseDescendantValidation(child.Children(), nextOwner, nextPath, nestedByNode, out, indent, choiceCounter)
		}
	}
}

func (g *goEmitter) emitScalarFieldValidationAt(f fieldInfo, owner, pathExpr string, out *strings.Builder, indent string) {
	base := fieldConcreteType(f)
	ref := owner + "." + f.ident
	if _, ok := g.intRangeTypes[base]; ok {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif _, err := New%s(%s.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, ref, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sif _, err := New%s(%s.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, ref, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif _, err := New%s(v.Get()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}
	if _, ok := g.stringLengthTypes[base]; ok {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif _, err := New%s(%s.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, ref, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sif _, err := New%s(%s.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, ref, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif _, err := New%s(v.String()); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, base, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}

	g.emitStringValueValidationAt(f, owner, pathExpr, out, indent)

	if info, ok := stringPatternInfo(f.node); ok {
		g.helpers["patternValidate"] = true
		patterns := stringPatternLiteral(info)
		_, isLengthType := g.stringLengthTypes[base]
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				valueExpr := "*" + ref
				if isLengthType {
					valueExpr = ref + ".String()"
				}
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, patterns, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				valueExpr := ref
				if isLengthType {
					valueExpr = ref + ".String()"
				}
				fmt.Fprintf(out, "%sif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, patterns, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			valueExpr := "v"
			if isLengthType {
				valueExpr = "v.String()"
			}
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif err := cambiumValidateStringPatterns(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, valueExpr, patterns, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}

	if info, ok := binaryTypeInfo(f.node); ok {
		g.helpers["binaryParse"] = true
		ranges := binaryLengthRangeLiteral(info)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif err := cambiumValidateBinaryString(*%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, ref, ranges, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sif err := cambiumValidateBinaryString(%s, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, ref, ranges, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif err := cambiumValidateBinaryString(v, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, ranges, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}

	if info, ok := decimal64TypeInfo(f.node); ok {
		fd, ranges := decimal64ValidationArgs(info)
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif err := cambiumValidateDecimal64Value(*%s, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, ref, fd, ranges, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sif err := cambiumValidateDecimal64Value(%s, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, ref, fd, ranges, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif err := cambiumValidateDecimal64Value(v, %d, %s); err != nil { return cambiumValidationError(%s, err.Error()) }\n", indent, fd, ranges, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}

	if f.isEnum || f.isIdentityref {
		method := "AsName"
		message := "invalid enum value"
		if f.isIdentityref {
			method = "AsJSONName"
			message = "invalid identityref value"
		}
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil && %s.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", indent, ref, ref, method, pathExpr, message)
			} else {
				fmt.Fprintf(out, "%sif %s.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", indent, ref, method, pathExpr, message)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif v.%s() == \"\" { return cambiumValidationError(%s, %q) }\n", indent, method, pathExpr, message)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}

	if f.isUnion {
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if f.optional {
				fmt.Fprintf(out, "%sif %s != nil {\n", indent, ref)
				fmt.Fprintf(out, "%s\tif err := %s.validate(%s); err != nil { return err }\n", indent, ref, pathExpr)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sif err := %s.validate(%s); err != nil { return err }\n", indent, ref, pathExpr)
			}
		case cambium.SchemaNodeKindLeafList:
			itemsExpr := fieldItemsExpr(owner, f.ident, f)
			fmt.Fprintf(out, "%sfor _, v := range %s {\n", indent, itemsExpr)
			fmt.Fprintf(out, "%s\tif err := v.validate(%s); err != nil { return err }\n", indent, pathExpr)
			fmt.Fprintf(out, "%s}\n", indent)
		}
	}
}

func mandatoryFieldMissingExpr(owner string, f fieldInfo) string {
	ref := owner + "." + f.ident
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindAnyData:
		if f.optional {
			return ref + " == nil"
		}
	}
	return ""
}

func choiceCasePathExpr(path []string) string {
	pathExpr := "path"
	for _, elem := range path {
		pathExpr = fmt.Sprintf("cambiumJoinPath(%s, %q)", pathExpr, elem)
	}
	return pathExpr
}

func validationChoices(children []cambium.SchemaNodeRef) []cambium.SchemaNodeRef {
	var out []cambium.SchemaNodeRef
	for _, child := range children {
		if child.Kind() == cambium.SchemaNodeKindChoice {
			out = append(out, child)
		}
	}
	return out
}

func (g *goEmitter) choiceCasePresenceExpr(owner string, c cambium.SchemaNodeRef, byNode map[cambium.SchemaNodeRef]fieldInfo) string {
	var exprs []string
	for child := range c.DataChildren(true).Iter() {
		if f, ok := byNode[child]; ok {
			exprs = append(exprs, fieldPresentExpr(owner, f))
		}
	}
	if len(exprs) == 0 {
		return "false"
	}
	return strings.Join(exprs, " || ")
}

func (g *goEmitter) choiceDefaultEmissionGuard(owner string, f fieldInfo, byNode map[cambium.SchemaNodeRef]fieldInfo) string {
	choice, caseNode, ok := nearestChoiceCase(f.node)
	if !ok {
		return ""
	}
	caseExpr := g.choiceCasePresenceExpr(owner, caseNode, byNode)
	defaultCase, hasDefaultCase := choice.DefaultValue()
	if !hasDefaultCase {
		return caseExpr
	}
	if defaultCase != caseNode.Name() {
		return caseExpr
	}
	anyCaseExpr := g.choiceAnyCasePresenceExpr(owner, choice, byNode)
	return "(" + caseExpr + " || !(" + anyCaseExpr + "))"
}

func nearestChoiceCase(node cambium.SchemaNodeRef) (cambium.SchemaNodeRef, cambium.SchemaNodeRef, bool) {
	ancestors := node.Ancestors()
	for i := len(ancestors) - 1; i >= 0; i-- {
		if ancestors[i].Kind() != cambium.SchemaNodeKindCase {
			continue
		}
		for j := i - 1; j >= 0; j-- {
			if ancestors[j].Kind() == cambium.SchemaNodeKindChoice {
				return ancestors[j], ancestors[i], true
			}
		}
	}
	return cambium.SchemaNodeRef{}, cambium.SchemaNodeRef{}, false
}

func (g *goEmitter) choiceAnyCasePresenceExpr(owner string, choice cambium.SchemaNodeRef, byNode map[cambium.SchemaNodeRef]fieldInfo) string {
	var exprs []string
	for c := range choice.Children().Iter() {
		if c.Kind() != cambium.SchemaNodeKindCase {
			continue
		}
		expr := g.choiceCasePresenceExpr(owner, c, byNode)
		if expr != "false" {
			exprs = append(exprs, expr)
		}
	}
	if len(exprs) == 0 {
		return "false"
	}
	return strings.Join(exprs, " || ")
}

func fieldPresentExpr(owner string, f fieldInfo) string {
	ref := owner + "." + f.ident
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if f.optional {
			return ref + " != nil"
		}
		return "true"
	case cambium.SchemaNodeKindLeafList, cambium.SchemaNodeKindList:
		return validationLenExpr(owner, f) + " > 0"
	case cambium.SchemaNodeKindAnyData:
		if f.optional {
			return ref + " != nil"
		}
		return "true"
	case cambium.SchemaNodeKindContainer:
		if f.optional {
			return ref + " != nil"
		}
		return ref + ".hasContent()"
	case cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
		return ref + " != nil"
	default:
		return "false"
	}
}

func validationLenExpr(owner string, f fieldInfo) string {
	if strings.HasPrefix(f.goType, "UserOrderedVec[") {
		return owner + "." + f.ident + ".Len()"
	}
	return "len(" + owner + "." + f.ident + ")"
}

func (g *goEmitter) emitUniqueValidation(f fieldInfo, out *strings.Builder) {
	g.emitUniqueValidationAt(f, "n", fmt.Sprintf("cambiumJoinPath(path, %q)", f.wire), out, "\t")
}

func (g *goEmitter) emitUniqueValidationAt(f fieldInfo, owner, pathExpr string, out *strings.Builder, indent string) {
	uniques := f.node.UniqueConstraints()
	if len(uniques) == 0 {
		return
	}
	itemsExpr := fieldItemsExpr(owner, f.ident, f)
	for i, unique := range uniques {
		var accessors []uniqueLeafAccessor
		for _, leaf := range unique.Leafs() {
			accessor, ok := g.uniqueLeafAccessor(f, leaf)
			if !ok {
				g.recordEmitError(fmt.Errorf("unsupported unique leaf path %s", leaf.Path()))
				continue
			}
			accessors = append(accessors, accessor)
		}
		if len(accessors) == 0 {
			continue
		}
		seenName := fmt.Sprintf("seen%sUnique%d", f.ident, i)
		fmt.Fprintf(out, "%s%s := make(map[string]struct{})\n", indent, seenName)
		fmt.Fprintf(out, "%sfor _, entry := range %s {\n", indent, itemsExpr)
		conds := make([]string, 0, len(accessors))
		parts := make([]string, 0, len(accessors))
		for _, accessor := range accessors {
			if accessor.presentExpr != "true" {
				conds = append(conds, accessor.presentExpr)
			}
			parts = append(parts, accessor.keyExpr)
		}
		cond := "true"
		if len(conds) > 0 {
			cond = strings.Join(conds, " && ")
		}
		fmt.Fprintf(out, "%s\tif %s {\n", indent, cond)
		keyExpr := parts[0]
		if len(parts) > 1 {
			keyExpr = "strings.Join([]string{" + strings.Join(parts, ", ") + `}, "\x00")`
		}
		fmt.Fprintf(out, "%s\t\tkey := %s\n", indent, keyExpr)
		fmt.Fprintf(out, "%s\t\tif _, ok := %s[key]; ok {\n", indent, seenName)
		fmt.Fprintf(out, "%s\t\t\treturn cambiumValidationError(%s, %q)\n", indent, pathExpr, "unique violation")
		fmt.Fprintf(out, "%s\t\t}\n", indent)
		fmt.Fprintf(out, "%s\t\t%s[key] = struct{}{}\n", indent, seenName)
		fmt.Fprintf(out, "%s\t}\n", indent)
		fmt.Fprintf(out, "%s}\n", indent)
	}
}

type uniqueLeafAccessor struct {
	field       fieldInfo
	presentExpr string
	keyExpr     string
}

func (g *goEmitter) uniqueLeafAccessor(listField fieldInfo, leaf cambium.SchemaNodeRef) (uniqueLeafAccessor, bool) {
	path, ok := uniqueLeafPath(listField.node, leaf)
	if !ok || len(path) == 0 {
		return uniqueLeafAccessor{}, false
	}
	owner := "entry"
	presentConds := []string{}
	fields := g.collectFields(fieldConcreteType(listField), orderedListChildren(listField.node.DataChildren(true), listField.node.ListKeys()))
	for i, node := range path {
		if node.Kind() == cambium.SchemaNodeKindChoice || node.Kind() == cambium.SchemaNodeKindCase {
			continue
		}
		field, ok := findFieldForNode(fields, node)
		if !ok {
			return uniqueLeafAccessor{}, false
		}
		if i == len(path)-1 {
			present, keyExpr := g.uniqueFieldEffectivePresenceAndKey(owner, field, fields)
			if present != "true" {
				presentConds = append(presentConds, present)
			}
			presentExpr := "true"
			if len(presentConds) > 0 {
				presentExpr = strings.Join(presentConds, " && ")
			}
			return uniqueLeafAccessor{
				field:       field,
				presentExpr: presentExpr,
				keyExpr:     keyExpr,
			}, true
		}
		if node.Kind() != cambium.SchemaNodeKindContainer {
			return uniqueLeafAccessor{}, false
		}
		ref := owner + "." + field.ident
		if field.optional {
			presentConds = append(presentConds, ref+" != nil")
		}
		owner = ref
		fields = g.collectFields(fieldConcreteType(field), g.orderedChildrenList(node.DataChildren(true)))
	}
	return uniqueLeafAccessor{}, false
}

func (g *goEmitter) uniqueFieldEffectivePresenceAndKey(owner string, f fieldInfo, fields []fieldInfo) (string, string) {
	actualPresent := uniqueFieldPresentExpr(owner, f)
	actualKey := g.jsonLiteralExpr(uniqueFieldValueExpr(owner, f), f)
	defaultValue, hasDefault := f.node.DefaultValue()
	if !hasDefault || !f.optional {
		return actualPresent, actualKey
	}
	defaultPresent := g.choiceDefaultEmissionGuard(owner, f, fieldsByNode(fields))
	if defaultPresent == "" {
		defaultPresent = "true"
	}
	effectivePresent := actualPresent
	if actualPresent != "true" {
		effectivePresent = "(" + actualPresent + " || " + defaultPresent + ")"
	}
	defaultKey := g.defaultJSONLiteralExpr(f, defaultValue)
	keyExpr := fmt.Sprintf("func() string { if %s { return %s }; return %s }()", actualPresent, actualKey, defaultKey)
	return effectivePresent, keyExpr
}

func uniqueLeafPath(list, leaf cambium.SchemaNodeRef) ([]cambium.SchemaNodeRef, bool) {
	ancestors := leaf.Ancestors()
	for i, ancestor := range ancestors {
		if ancestor == list {
			path := append([]cambium.SchemaNodeRef(nil), ancestors[i+1:]...)
			path = append(path, leaf)
			return path, true
		}
	}
	return nil, false
}

func findFieldForNode(fields []fieldInfo, node cambium.SchemaNodeRef) (fieldInfo, bool) {
	for _, field := range fields {
		if field.node == node {
			return field, true
		}
	}
	return fieldInfo{}, false
}

func uniqueFieldPresentExpr(owner string, f fieldInfo) string {
	if f.optional {
		return owner + "." + f.ident + " != nil"
	}
	return "true"
}

func uniqueFieldValueExpr(owner string, f fieldInfo) string {
	ref := owner + "." + f.ident
	if f.optional && !f.isUnion {
		return "(*" + ref + ")"
	}
	return ref
}

func (g *goEmitter) uniqueStringExpr(valueRef string, f fieldInfo) string {
	base := scalarBaseType(f)
	if f.isUnion {
		return valueRef + ".String()"
	}
	if f.isIdentityref {
		return valueRef + ".AsJSONName()"
	}
	switch f.jsonKind {
	case "InstanceIdentifier":
		return valueRef + ".String()"
	case "String":
		if base == "string" {
			return valueRef
		}
		return valueRef + ".String()"
	case "Bool":
		g.helpers["strconv"] = true
		return "strconv.FormatBool(" + valueRef + ")"
	case "BareNumber", "QuotedNumber":
		if f.isNewtype || base == "Decimal64" {
			return valueRef + ".String()"
		}
		g.helpers["strconv"] = true
		if f.intSigned {
			return "strconv.FormatInt(int64(" + valueRef + "), 10)"
		}
		return "strconv.FormatUint(uint64(" + valueRef + "), 10)"
	case "Empty":
		return `""`
	default:
		return valueRef + ".String()"
	}
}

func (g *goEmitter) emitRootStruct(fields []fieldInfo, out *strings.Builder) {
	name := g.modulePascal

	g.emitStructDefinition(name, fields, out)
	g.emitFieldOrder(name, fields, out)

	// Root XML serializer.
	fmt.Fprintf(out, "func (m *%s) ToXML() string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	for _, f := range fields {
		g.emitTopLevelXML(f, out)
	}
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")

	// Root JSON serializer.
	g.helpers["jsonIndent"] = true
	fmt.Fprintf(out, "func (m *%s) ToJSONIETF() string {\n", name)
	out.WriteString("\treturn m.ToJSONIETFWithDefaults(WithDefaultsExplicit)\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (m *%s) ToJSONIETFWithDefaults(mode WithDefaultsMode) string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	out.WriteString("\tw := &b\n")
	if len(fields) == 0 {
		out.WriteString("\tw.WriteString(\"{}\\n\")\n")
		out.WriteString("\treturn b.String()\n")
		out.WriteString("}\n\n")
		g.emitRootParser(name, fields, out)
		g.emitValidationMethods(name, fields, g.documentValidationSourceChildren(), "/"+g.moduleName, out)
		g.emitCambiumStructImpl(name, out)
		fmt.Fprintf(out, "// %sModuleNS is the XML namespace of module %q.\n", name, g.moduleName)
		fmt.Fprintf(out, "const %sModuleNS = %q\n\n", name, g.ns)
		fmt.Fprintf(out, "// %sModuleName is the YANG module name.\n", name)
		fmt.Fprintf(out, "const %sModuleName = %q\n\n", name, g.moduleName)
		return
	}
	out.WriteString("\tw.WriteByte('{')\n")
	out.WriteString("\tfirst := true\n")
	byNode := fieldsByNode(fields)
	for _, f := range fields {
		g.emitTopLevelJSON(f, byNode, out)
	}
	out.WriteString("\tcambiumJSONIndent(w, 0)\n")
	out.WriteString("\tw.WriteByte('}')\n")
	out.WriteString("\tw.WriteByte('\\n')\n")
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")

	g.emitRootParser(name, fields, out)
	g.emitValidationMethods(name, fields, g.documentValidationSourceChildren(), "/"+g.moduleName, out)
	g.emitCambiumStructImpl(name, out)

	// Module constants.
	fmt.Fprintf(out, "// %sModuleNS is the XML namespace of module %q.\n", name, g.moduleName)
	fmt.Fprintf(out, "const %sModuleNS = %q\n\n", name, g.ns)
	fmt.Fprintf(out, "// %sModuleName is the YANG module name.\n", name)
	fmt.Fprintf(out, "const %sModuleName = %q\n\n", name, g.moduleName)
}

func (g *goEmitter) emitRootParser(name string, fields []fieldInfo, out *strings.Builder) {
	g.helpers["jsonParse"] = true
	fmt.Fprintf(out, "func FromJSONIETF(data []byte) (*%s, error) {\n", name)
	out.WriteString("\tobj, err := cambiumJSONDecodeObject(data)\n")
	out.WriteString("\tif err != nil { return nil, err }\n")
	fmt.Fprintf(out, "\tout := &%s{}\n", name)
	out.WriteString("\tif err := out.parseJSONIETF(obj); err != nil { return nil, err }\n")
	out.WriteString("\tif err := out.Validate(); err != nil { return nil, err }\n")
	out.WriteString("\treturn out, nil\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (m *%s) parseJSONIETF(obj map[string]any) error {\n", name)
	out.WriteString("\tif m == nil { return nil }\n")
	byNode := fieldsByNode(fields)
	g.emitJSONUnknownFieldCheck(fields, func(f fieldInfo) string {
		moduleName := f.moduleName
		if moduleName == "" {
			moduleName = g.moduleName
		}
		return moduleName + ":" + f.wire
	}, out)
	for _, f := range fields {
		moduleName := f.moduleName
		if moduleName == "" {
			moduleName = g.moduleName
		}
		g.emitJSONParseField("m", moduleName+":"+f.wire, f, byNode, out)
	}
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitOperationDocuments(out *strings.Builder) {
	for rpc := range g.module.RPCs().Iter() {
		input := g.operationInputSource(rpc)
		output := g.operationOutputSource(rpc)
		switch {
		case input.Kind() != cambium.SchemaNodeKindUnknown:
			g.emitOperationDocument(rpc, input, "RPC", "/"+rpc.Module().Name()+"/"+rpc.Name(), out)
			if output.Kind() != cambium.SchemaNodeKindUnknown {
				g.emitOperationDocument(rpc, output, "RPCOutput", "/"+rpc.Module().Name()+"/"+rpc.Name()+"/output", out)
			}
		case output.Kind() != cambium.SchemaNodeKindUnknown:
			g.emitOperationDocument(rpc, output, "RPC", "/"+rpc.Module().Name()+"/"+rpc.Name(), out)
		default:
			g.emitOperationDocument(rpc, rpc, "RPC", "/"+rpc.Module().Name()+"/"+rpc.Name(), out)
		}
	}
	for notif := range g.module.Notifications().Iter() {
		g.emitOperationDocument(notif, notif, "Notification", "/"+notif.Module().Name()+"/"+notif.Name(), out)
	}
}

func (g *goEmitter) emitOperationDocument(op, source cambium.SchemaNodeRef, suffix, validationPath string, out *strings.Builder) {
	structName := g.modulePascal + toPascalCase(op.Name()) + suffix
	fields := g.collectFields(structName, g.operationSourcePayloadChildren(op, source))

	g.emitStructDefinition(structName, fields, out)
	g.emitFieldOrder(structName, fields, out)
	g.emitStructWriters(structName, fields, op.Module().Name(), op.Namespace(), out)
	g.emitStructParser(structName, fields, op.Module().Name(), out)
	g.emitOperationRootSerializers(structName, op, out)
	g.emitOperationRootParser(structName, op, out)
	g.emitValidationMethods(structName, fields, g.operationSourceValidationChildren(source), validationPath, out)
	g.emitCambiumStructImpl(structName, out)

	for _, f := range fields {
		if isStructKind(f.node.Kind()) {
			g.emitStructRec(f.node, fieldConcreteType(f), out)
		}
	}
}

func (g *goEmitter) operationSourcePayloadChildren(op, source cambium.SchemaNodeRef) []cambium.SchemaNodeRef {
	if source.Kind() != cambium.SchemaNodeKindUnknown && source != op {
		return g.orderedChildrenList(source.DataChildren(true))
	}
	return g.orderedChildrenList(op.DataChildren(true))
}

func (g *goEmitter) operationSourceValidationChildren(source cambium.SchemaNodeRef) []cambium.SchemaNodeRef {
	if source.Kind() != cambium.SchemaNodeKindUnknown {
		return schemaChildrenList(source.Children())
	}
	return nil
}

func (g *goEmitter) operationPayloadChildren(op cambium.SchemaNodeRef) []cambium.SchemaNodeRef {
	source := g.operationPayloadSource(op)
	if source.Name() != "" {
		return g.orderedChildrenList(source.DataChildren(true))
	}
	return g.orderedChildrenList(op.DataChildren(true))
}

func (g *goEmitter) operationPayloadSource(op cambium.SchemaNodeRef) cambium.SchemaNodeRef {
	if op.Kind() == cambium.SchemaNodeKindRPC || op.Kind() == cambium.SchemaNodeKindAction {
		var output cambium.SchemaNodeRef
		for child := range op.Children().Iter() {
			switch child.Kind() {
			case cambium.SchemaNodeKindInput:
				if child.DataChildren(true).Len() > 0 {
					return child
				}
			case cambium.SchemaNodeKindOutput:
				output = child
			}
		}
		if output.Name() != "" {
			return output
		}
	}
	return op
}

func (g *goEmitter) operationInputSource(op cambium.SchemaNodeRef) cambium.SchemaNodeRef {
	if op.Kind() != cambium.SchemaNodeKindRPC && op.Kind() != cambium.SchemaNodeKindAction {
		return cambium.SchemaNodeRef{}
	}
	for child := range op.Children().Iter() {
		if child.Kind() == cambium.SchemaNodeKindInput && child.DataChildren(true).Len() > 0 {
			return child
		}
	}
	return cambium.SchemaNodeRef{}
}

func (g *goEmitter) operationOutputSource(op cambium.SchemaNodeRef) cambium.SchemaNodeRef {
	if op.Kind() != cambium.SchemaNodeKindRPC && op.Kind() != cambium.SchemaNodeKindAction {
		return cambium.SchemaNodeRef{}
	}
	for child := range op.Children().Iter() {
		if child.Kind() == cambium.SchemaNodeKindOutput && child.DataChildren(true).Len() > 0 {
			return child
		}
	}
	return cambium.SchemaNodeRef{}
}

func (g *goEmitter) validationSourceChildren(node cambium.SchemaNodeRef) []cambium.SchemaNodeRef {
	if node.Kind() == cambium.SchemaNodeKindRPC || node.Kind() == cambium.SchemaNodeKindAction {
		source := g.operationPayloadSource(node)
		if source.Name() != "" {
			return schemaChildrenList(source.Children())
		}
	}
	return schemaChildrenList(node.Children())
}

func (g *goEmitter) emitOperationRootSerializers(name string, op cambium.SchemaNodeRef, out *strings.Builder) {
	wire := op.Name()
	moduleName := op.Module().Name()
	ns := op.Namespace()
	nsAttr := staticXMLNSAttr(ns)

	fmt.Fprintf(out, "func (n *%s) ToXML() string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	out.WriteString("\tif n.hasContent() {\n")
	fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s>\\n\")\n", wire, nsAttr)
	out.WriteString("\t\tn.writeXML(&b, 1)\n")
	fmt.Fprintf(out, "\t\tb.WriteString(\"</%s>\\n\")\n", wire)
	out.WriteString("\t} else {\n")
	fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s/>\\n\")\n", wire, nsAttr)
	out.WriteString("\t}\n")
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")

	g.helpers["jsonIndent"] = true
	fmt.Fprintf(out, "func (n *%s) ToJSONIETF() string {\n", name)
	out.WriteString("\treturn n.ToJSONIETFWithDefaults(WithDefaultsExplicit)\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (n *%s) ToJSONIETFWithDefaults(mode WithDefaultsMode) string {\n", name)
	out.WriteString("\tvar b strings.Builder\n")
	out.WriteString("\tw := &b\n")
	out.WriteString("\tw.WriteByte('{')\n")
	out.WriteString("\tcambiumJSONIndent(w, 1)\n")
	fmt.Fprintf(out, "\tw.WriteString(\"\\\"%s:%s\\\": \")\n", moduleName, wire)
	out.WriteString("\tn.writeJSONWithDefaults(w, 1, mode)\n")
	out.WriteString("\tcambiumJSONIndent(w, 0)\n")
	out.WriteString("\tw.WriteByte('}')\n")
	out.WriteString("\tw.WriteByte('\\n')\n")
	out.WriteString("\treturn b.String()\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitOperationRootParser(name string, op cambium.SchemaNodeRef, out *strings.Builder) {
	g.helpers["jsonParse"] = true
	key := op.Module().Name() + ":" + op.Name()
	fmt.Fprintf(out, "func From%sJSONIETF(data []byte) (*%s, error) {\n", name, name)
	out.WriteString("\tobj, err := cambiumJSONDecodeObject(data)\n")
	out.WriteString("\tif err != nil { return nil, err }\n")
	fmt.Fprintf(out, "\tconst rootKey = %q\n", key)
	out.WriteString("\tfor _, key := range cambiumSortedJSONKeys(obj) {\n")
	out.WriteString("\t\tif key != rootKey {\n")
	out.WriteString("\t\t\treturn nil, fmt.Errorf(\"unknown JSON_IETF field %q\", key)\n")
	out.WriteString("\t\t}\n")
	out.WriteString("\t}\n")
	out.WriteString("\traw, ok := obj[rootKey]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn nil, fmt.Errorf(\"missing JSON_IETF field %q\", rootKey)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tchildObj, err := cambiumJSONObject(raw, rootKey)\n")
	out.WriteString("\tif err != nil { return nil, err }\n")
	fmt.Fprintf(out, "\tout := &%s{}\n", name)
	out.WriteString("\tif err := out.parseJSONIETF(childObj); err != nil { return nil, err }\n")
	out.WriteString("\tif err := out.Validate(); err != nil { return nil, err }\n")
	out.WriteString("\treturn out, nil\n")
	out.WriteString("}\n\n")
}

func (g *goEmitter) emitTopLevelXML(f fieldInfo, out *strings.Builder) {
	wire := f.wire
	ident := f.ident
	nsAttr := staticXMLNSAttr(f.namespace)

	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if f.jsonKind == "Empty" {
			if f.optional {
				fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
				fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s\" + cambiumMetadataXMLAttrs(m.CambiumMetadata[%q]) + \"/>\\n\")\n", wire, nsAttr, wire)
				out.WriteString("\t}\n")
			} else {
				fmt.Fprintf(out, "\tb.WriteString(\"<%s%s\" + cambiumMetadataXMLAttrs(m.CambiumMetadata[%q]) + \"/>\\n\")\n", wire, nsAttr, wire)
			}
			return
		}
		if f.optional {
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			if f.isIdentityref {
				g.emitTopIdentityrefLeafValue(wire, nsAttr, "(*m."+ident+")", out)
				out.WriteString("\t}\n")
				return
			}
			if f.jsonKind == "InstanceIdentifier" {
				g.emitTopInstanceIdentifierLeafValue(wire, nsAttr, "(*m."+ident+")", out)
				out.WriteString("\t}\n")
				return
			}
			valueRef := "(*m." + ident + ")"
			if f.isUnion {
				valueRef = "m." + ident
				g.emitTopUnionLeafValue(wire, nsAttr, valueRef, out)
				out.WriteString("\t}\n")
				return
			}
			expr := g.xmlValueExpr(valueRef, f)
			if strings.Contains(expr, "cambiumXMLEscapeText") {
				g.helpers["xmlEscape"] = true
			}
			fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s\" + cambiumMetadataXMLAttrs(m.CambiumMetadata[%q]) + \">\" + %s + \"</%s>\\n\")\n", wire, nsAttr, wire, expr, wire)
			out.WriteString("\t}\n")
		} else {
			if f.isIdentityref {
				g.emitTopIdentityrefLeafValue(wire, nsAttr, "m."+ident, out)
				return
			}
			if f.jsonKind == "InstanceIdentifier" {
				g.emitTopInstanceIdentifierLeafValue(wire, nsAttr, "m."+ident, out)
				return
			}
			if f.isUnion {
				g.emitTopUnionLeafValue(wire, nsAttr, "m."+ident, out)
				return
			}
			expr := g.xmlValueExpr("m."+ident, f)
			if strings.Contains(expr, "cambiumXMLEscapeText") {
				g.helpers["xmlEscape"] = true
			}
			fmt.Fprintf(out, "\tb.WriteString(\"<%s%s\" + cambiumMetadataXMLAttrs(m.CambiumMetadata[%q]) + \">\" + %s + \"</%s>\\n\")\n", wire, nsAttr, wire, expr, wire)
		}
	case cambium.SchemaNodeKindLeafList:
		itemsExpr := g.emitCollectionItems("m", f, out, "\t")
		fmt.Fprintf(out, "\tfor _, v := range %s {\n", itemsExpr)
		if f.isIdentityref {
			g.emitTopIdentityrefLeafValue(wire, nsAttr, "v", out)
			out.WriteString("\t}\n")
			return
		}
		if f.jsonKind == "InstanceIdentifier" {
			g.emitTopInstanceIdentifierLeafValue(wire, nsAttr, "v", out)
			out.WriteString("\t}\n")
			return
		}
		if f.isUnion {
			g.emitTopUnionLeafValue(wire, nsAttr, "v", out)
			out.WriteString("\t}\n")
			return
		}
		expr := g.xmlValueExpr("v", f)
		if strings.Contains(expr, "cambiumXMLEscapeText") {
			g.helpers["xmlEscape"] = true
		}
		fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s\" + cambiumMetadataXMLAttrs(m.CambiumMetadata[%q]) + \">\" + %s + \"</%s>\\n\")\n", wire, nsAttr, wire, expr, wire)
		out.WriteString("\t}\n")
	case cambium.SchemaNodeKindAnyData:
		if f.optional {
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			g.emitTopAnyDataXML(wire, f, "(*m."+ident+")", out)
			out.WriteString("\t}\n")
		} else {
			g.emitTopAnyDataXML(wire, f, "m."+ident, out)
		}
	case cambium.SchemaNodeKindContainer:
		if f.optional {
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			fmt.Fprintf(out, "\t\tif m.%s.hasContent() {\n", ident)
			fmt.Fprintf(out, "\t\t\tb.WriteString(\"<%s%s>\\n\")\n", wire, nsAttr)
			fmt.Fprintf(out, "\t\t\tm.%s.writeXML(&b, 1)\n", ident)
			fmt.Fprintf(out, "\t\t\tb.WriteString(\"</%s>\\n\")\n", wire)
			out.WriteString("\t\t} else {\n")
			fmt.Fprintf(out, "\t\t\tb.WriteString(\"<%s%s/>\\n\")\n", wire, nsAttr)
			out.WriteString("\t\t}\n")
			out.WriteString("\t}\n")
		} else {
			fmt.Fprintf(out, "\tif m.%s.hasContent() {\n", ident)
			fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s>\\n\")\n", wire, nsAttr)
			fmt.Fprintf(out, "\t\tm.%s.writeXML(&b, 1)\n", ident)
			fmt.Fprintf(out, "\t\tb.WriteString(\"</%s>\\n\")\n", wire)
			out.WriteString("\t}\n")
		}
	case cambium.SchemaNodeKindList:
		itemsExpr := g.emitCollectionItems("m", f, out, "\t")
		fmt.Fprintf(out, "\tfor _, entry := range %s {\n", itemsExpr)
		fmt.Fprintf(out, "\t\tb.WriteString(\"<%s%s>\\n\")\n", wire, nsAttr)
		out.WriteString("\t\tentry.writeXML(&b, 1)\n")
		fmt.Fprintf(out, "\t\tb.WriteString(\"</%s>\\n\")\n", wire)
		out.WriteString("\t}\n")
	}
}

func (g *goEmitter) emitTopLevelJSON(f fieldInfo, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder) {
	moduleName := f.moduleName
	if moduleName == "" {
		moduleName = g.moduleName
	}
	wire := fmt.Sprintf("%s:%s", moduleName, f.wire)
	ident := f.ident

	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if f.jsonKind == "Empty" {
			if f.optional {
				fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
				out.WriteString("\t\tif !first { w.WriteByte(',') }\n")
				out.WriteString("\t\tfirst = false\n")
				out.WriteString("\t\tcambiumJSONIndent(w, 1)\n")
				fmt.Fprintf(out, "\t\tw.WriteString(\"\\\"%s\\\": [null]\")\n", wire)
				fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t}\n")
			} else {
				out.WriteString("\tif !first { w.WriteByte(',') }\n")
				out.WriteString("\tfirst = false\n")
				out.WriteString("\tcambiumJSONIndent(w, 1)\n")
				fmt.Fprintf(out, "\tw.WriteString(\"\\\"%s\\\": [null]\")\n", wire)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
			return
		}
		if f.optional {
			defaultValue, hasDefault := f.node.DefaultValue()
			valueRef := "(*m." + ident + ")"
			if f.isUnion {
				valueRef = "m." + ident
			}
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			if hasDefault {
				fmt.Fprintf(out, "\t\tif mode != WithDefaultsTrim || %s != %s {\n", g.jsonLiteralExpr(valueRef, f), g.defaultJSONLiteralExpr(f, defaultValue))
				g.emitScalarJSON(wire, "w", "1", valueRef, f, out)
				fmt.Fprintf(out, "\t\t\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t\t}\n")
			} else {
				g.emitScalarJSON(wire, "w", "1", valueRef, f, out)
				fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
			out.WriteString("\t}\n")
			if hasDefault {
				defaultCond := "m." + ident + " == nil && (mode == WithDefaultsAll || mode == WithDefaultsAllTagged)"
				if guard := g.choiceDefaultEmissionGuard("m", f, byNode); guard != "" {
					defaultCond += " && (" + guard + ")"
				}
				fmt.Fprintf(out, "\tif %s {\n", defaultCond)
				g.emitScalarJSONLiteral(wire, "w", "1", g.defaultJSONLiteralExpr(f, defaultValue), out)
				out.WriteString("\t}\n")
			}
		} else {
			if defaultValue, hasDefault := f.node.DefaultValue(); hasDefault {
				fmt.Fprintf(out, "\tif mode != WithDefaultsTrim || %s != %s {\n", g.jsonLiteralExpr("m."+ident, f), g.defaultJSONLiteralExpr(f, defaultValue))
				g.emitScalarJSON(wire, "w", "1", "m."+ident, f, out)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
				out.WriteString("\t}\n")
			} else {
				g.emitScalarJSON(wire, "w", "1", "m."+ident, f, out)
				fmt.Fprintf(out, "\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
			}
		}
	case cambium.SchemaNodeKindLeafList:
		defaults := f.node.DefaultValues()
		defaultVar := ""
		if len(defaults) > 0 {
			defaultVar = g.emitLeafListDefaultCheck("m", f, defaults, out, "\t")
		}
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\tif m.%s.Len() > 0 {\n", ident)
		} else {
			fmt.Fprintf(out, "\tif len(m.%s) > 0 {\n", ident)
		}
		if defaultVar != "" {
			fmt.Fprintf(out, "\t\tif mode != WithDefaultsTrim || !%s {\n", defaultVar)
		}
		itemsExpr := g.emitCollectionItems("m", f, out, "\t\t")
		g.emitLeafListJSONArray(wire, "w", "1", "2", itemsExpr, f, out)
		fmt.Fprintf(out, "\t\tcambiumWriteMetadataJSON(w, 1, %q, m.CambiumMetadata[%q], &first)\n", wire, f.wire)
		if defaultVar != "" {
			out.WriteString("\t\t}\n")
		}
		out.WriteString("\t}\n")
		if len(defaults) > 0 {
			defaultCond := "mode == WithDefaultsAll || mode == WithDefaultsAllTagged"
			if guard := g.choiceDefaultEmissionGuard("m", f, byNode); guard != "" {
				defaultCond = "(" + defaultCond + ") && (" + guard + ")"
			}
			if strings.HasPrefix(f.goType, "UserOrderedVec[") {
				fmt.Fprintf(out, "\tif m.%s.Len() == 0 && %s {\n", ident, defaultCond)
			} else {
				fmt.Fprintf(out, "\tif len(m.%s) == 0 && %s {\n", ident, defaultCond)
			}
			g.emitDefaultLeafListJSONArray(wire, "w", "1", "2", defaults, f, out)
			out.WriteString("\t}\n")
		}
	case cambium.SchemaNodeKindAnyData:
		if f.optional {
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			g.emitAnyDataJSON(wire, "w", "1", "(*m."+ident+")", f, out)
			out.WriteString("\t}\n")
		} else {
			g.emitAnyDataJSON(wire, "w", "1", "m."+ident, f, out)
		}
	case cambium.SchemaNodeKindContainer:
		if f.optional {
			fmt.Fprintf(out, "\tif m.%s != nil {\n", ident)
			out.WriteString("\t\tif !first { w.WriteByte(',') }\n")
			out.WriteString("\t\tfirst = false\n")
			out.WriteString("\t\tcambiumJSONIndent(w, 1)\n")
			fmt.Fprintf(out, "\t\tw.WriteString(\"\\\"%s\\\": \")\n", wire)
			fmt.Fprintf(out, "\t\tm.%s.writeJSONWithDefaults(w, 1, mode)\n", ident)
			out.WriteString("\t}\n")
		} else {
			contentCond := "m." + ident + ".hasContentWithDefaults(mode)"
			if guard := g.choiceDefaultEmissionGuard("m", f, byNode); guard != "" {
				contentCond += " && (" + guard + ")"
			}
			fmt.Fprintf(out, "\tif %s {\n", contentCond)
			out.WriteString("\t\tif !first { w.WriteByte(',') }\n")
			out.WriteString("\t\tfirst = false\n")
			out.WriteString("\t\tcambiumJSONIndent(w, 1)\n")
			fmt.Fprintf(out, "\t\tw.WriteString(\"\\\"%s\\\": \")\n", wire)
			fmt.Fprintf(out, "\t\tm.%s.writeJSONWithDefaults(w, 1, mode)\n", ident)
			out.WriteString("\t}\n")
		}
	case cambium.SchemaNodeKindList:
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\tif m.%s.Len() > 0 {\n", ident)
		} else {
			fmt.Fprintf(out, "\tif len(m.%s) > 0 {\n", ident)
		}
		itemsExpr := g.emitCollectionItems("m", f, out, "\t\t")
		out.WriteString("\t\tif !first { w.WriteByte(',') }\n")
		out.WriteString("\t\tfirst = false\n")
		out.WriteString("\t\tcambiumJSONIndent(w, 1)\n")
		fmt.Fprintf(out, "\t\tw.WriteString(\"\\\"%s\\\": [\")\n", wire)
		fmt.Fprintf(out, "\t\tfor i, entry := range %s {\n", itemsExpr)
		out.WriteString("\t\t\tif i > 0 { w.WriteByte(',') }\n")
		out.WriteString("\t\t\tcambiumJSONIndent(w, 2)\n")
		out.WriteString("\t\t\tentry.writeJSONWithDefaults(w, 2, mode)\n")
		out.WriteString("\t\t}\n")
		out.WriteString("\t\tcambiumJSONIndent(w, 1)\n")
		out.WriteString("\t\tw.WriteByte(']')\n")
		out.WriteString("\t}\n")
	}
}

func (g *goEmitter) emitJSONUnknownFieldCheck(fields []fieldInfo, keyFor func(fieldInfo) string, out *strings.Builder) {
	out.WriteString("\tfor _, key := range cambiumSortedJSONKeys(obj) {\n")
	out.WriteString("\t\tswitch key {\n")
	for _, f := range fields {
		key := keyFor(f)
		labels := []string{strconv.Quote(key)}
		switch f.node.Kind() {
		case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindLeafList:
			labels = append(labels, strconv.Quote("@"+key))
		}
		fmt.Fprintf(out, "\t\tcase %s:\n", strings.Join(labels, ", "))
	}
	out.WriteString("\t\tdefault:\n")
	out.WriteString("\t\t\treturn fmt.Errorf(\"unknown JSON_IETF field %q\", key)\n")
	out.WriteString("\t\t}\n")
	out.WriteString("\t}\n")
}

func (g *goEmitter) emitJSONParseField(owner, key string, f fieldInfo, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder) {
	keyExpr := strconv.Quote(key)
	fmt.Fprintf(out, "\tif raw, ok := obj[%s]; ok {\n", keyExpr)
	out.WriteString("\t\t_ = raw\n")
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		if g.emitJSONParseScalarValue("v", "raw", keyExpr, f, out, "\t\t") {
			if f.optional && !f.isUnion {
				fmt.Fprintf(out, "\t\t%s.%s = &v\n", owner, f.ident)
			} else {
				fmt.Fprintf(out, "\t\t%s.%s = v\n", owner, f.ident)
			}
		}
	case cambium.SchemaNodeKindLeafList:
		base := fieldConcreteType(f)
		fmt.Fprintf(out, "\t\tarr, err := cambiumJSONArray(raw, %s)\n", keyExpr)
		out.WriteString("\t\tif err != nil { return err }\n")
		fmt.Fprintf(out, "\t\titems := make([]%s, 0, len(arr))\n", base)
		out.WriteString("\t\tfor _, item := range arr {\n")
		if g.emitJSONParseScalarValue("v", "item", keyExpr, f, out, "\t\t\t") {
			out.WriteString("\t\t\titems = append(items, v)\n")
		}
		out.WriteString("\t\t}\n")
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\t\t%s.%s = NewUserOrderedVec(items)\n", owner, f.ident)
		} else {
			fmt.Fprintf(out, "\t\t%s.%s = items\n", owner, f.ident)
		}
	case cambium.SchemaNodeKindContainer, cambium.SchemaNodeKindAction, cambium.SchemaNodeKindNotification:
		targetType := fieldConcreteType(f)
		fmt.Fprintf(out, "\t\tchildObj, err := cambiumJSONObject(raw, %s)\n", keyExpr)
		out.WriteString("\t\tif err != nil { return err }\n")
		fmt.Fprintf(out, "\t\tchild := %s{}\n", targetType)
		out.WriteString("\t\tif err := child.parseJSONIETF(childObj); err != nil { return err }\n")
		if f.optional {
			fmt.Fprintf(out, "\t\t%s.%s = &child\n", owner, f.ident)
		} else {
			fmt.Fprintf(out, "\t\t%s.%s = child\n", owner, f.ident)
		}
	case cambium.SchemaNodeKindList:
		entryType := fieldConcreteType(f)
		fmt.Fprintf(out, "\t\tarr, err := cambiumJSONArray(raw, %s)\n", keyExpr)
		out.WriteString("\t\tif err != nil { return err }\n")
		fmt.Fprintf(out, "\t\titems := make([]%s, 0, len(arr))\n", entryType)
		out.WriteString("\t\tfor _, item := range arr {\n")
		fmt.Fprintf(out, "\t\t\tchildObj, err := cambiumJSONObject(item, %s)\n", keyExpr)
		out.WriteString("\t\t\tif err != nil { return err }\n")
		fmt.Fprintf(out, "\t\t\tchild := %s{}\n", entryType)
		out.WriteString("\t\t\tif err := child.parseJSONIETF(childObj); err != nil { return err }\n")
		out.WriteString("\t\t\titems = append(items, child)\n")
		out.WriteString("\t\t}\n")
		if strings.HasPrefix(f.goType, "UserOrderedVec[") {
			fmt.Fprintf(out, "\t\t%s.%s = NewUserOrderedVec(items)\n", owner, f.ident)
		} else {
			fmt.Fprintf(out, "\t\t%s.%s = items\n", owner, f.ident)
		}
	case cambium.SchemaNodeKindAnyData:
		out.WriteString("\t\tb, err := cambiumJSONMarshalIndent(raw)\n")
		out.WriteString("\t\tif err != nil { return err }\n")
		out.WriteString("\t\tv := NewAnyData(\"\", b)\n")
		if f.optional {
			fmt.Fprintf(out, "\t\t%s.%s = &v\n", owner, f.ident)
		} else {
			fmt.Fprintf(out, "\t\t%s.%s = v\n", owner, f.ident)
		}
	}
	g.emitExplicitChoiceCaseParseValidation(owner, f, byNode, out, "\t\t")
	out.WriteString("\t}\n")
	if requiresJSONField(f.node) {
		fmt.Fprintf(out, "\tif _, ok := obj[%s]; !ok {\n", keyExpr)
		fmt.Fprintf(out, "\t\treturn fmt.Errorf(\"missing JSON_IETF field %%q\", %s)\n", keyExpr)
		out.WriteString("\t}\n")
	}
	switch f.node.Kind() {
	case cambium.SchemaNodeKindLeaf, cambium.SchemaNodeKindLeafList:
		module := f.node.Module()
		fmt.Fprintf(out, "\tif rawMeta, ok := obj[%q]; ok {\n", "@"+key)
		fmt.Fprintf(out, "\t\tif _, dataOK := obj[%s]; !dataOK { return fmt.Errorf(\"metadata JSON member %%q has no corresponding data node %%q\", %q, %s) }\n", keyExpr, "@"+key, keyExpr)
		fmt.Fprintf(out, "\t\titems, err := cambiumParseMetadataJSON(rawMeta, %q, %q, %q)\n", module.Name(), module.Prefix(), module.Namespace())
		out.WriteString("\t\tif err != nil { return err }\n")
		fmt.Fprintf(out, "\t\tif %s.CambiumMetadata == nil { %s.CambiumMetadata = make(map[string][]MetadataAnnotation) }\n", owner, owner)
		fmt.Fprintf(out, "\t\t%s.CambiumMetadata[%q] = items\n", owner, f.wire)
		out.WriteString("\t}\n")
	}
}

func (g *goEmitter) emitExplicitChoiceCaseParseValidation(owner string, f fieldInfo, byNode map[cambium.SchemaNodeRef]fieldInfo, out *strings.Builder, indent string) {
	if !f.node.IsChoiceDescendant() {
		return
	}
	_, caseNode, ok := nearestChoiceCase(f.node)
	if !ok {
		return
	}
	presenceExpr := fieldPresentExpr(owner, f)
	if presenceExpr == "true" {
		return
	}
	casePath := schemaPathElements(caseNode)
	if len(casePath) == 0 {
		return
	}
	var checks strings.Builder
	choiceCounter := 0
	g.emitSelectedCaseDescendantValidation(caseNode.Children(), owner, casePath, byNode, &checks, indent+"\t", &choiceCounter)
	if checks.Len() == 0 {
		return
	}
	fmt.Fprintf(out, "%sif !(%s) {\n", indent, presenceExpr)
	fmt.Fprintf(out, "%s\tpath := \"\"\n", indent)
	out.WriteString(checks.String())
	fmt.Fprintf(out, "%s}\n", indent)
}

func schemaPathElements(node cambium.SchemaNodeRef) []string {
	path := strings.Trim(node.Path(), "/")
	if path == "" {
		return nil
	}
	return strings.Split(path, "/")
}

func requiresJSONField(node cambium.SchemaNodeRef) bool {
	switch node.Kind() {
	case cambium.SchemaNodeKindLeaf:
		return requiresJSONLeafField(node)
	case cambium.SchemaNodeKindAnyData:
		return node.IsMandatory() && !node.IsChoiceDescendant() && len(node.Whens()) == 0
	case cambium.SchemaNodeKindContainer:
		return !node.IsPresenceContainer() && len(node.Whens()) == 0 && hasRequiredJSONDescendant(node)
	default:
		return false
	}
}

func requiresJSONLeafField(node cambium.SchemaNodeRef) bool {
	if node.Kind() != cambium.SchemaNodeKindLeaf {
		return false
	}
	if node.IsListKey() {
		return true
	}
	return node.IsMandatory() && !node.IsChoiceDescendant() && len(node.Whens()) == 0
}

func hasRequiredJSONDescendant(node cambium.SchemaNodeRef) bool {
	for child := range node.Children().Iter() {
		if len(child.Whens()) > 0 {
			continue
		}
		switch child.Kind() {
		case cambium.SchemaNodeKindLeaf:
			if requiresJSONLeafField(child) {
				return true
			}
		case cambium.SchemaNodeKindAnyData:
			if child.IsMandatory() && !child.IsChoiceDescendant() {
				return true
			}
		case cambium.SchemaNodeKindContainer:
			if !child.IsPresenceContainer() && hasRequiredJSONDescendant(child) {
				return true
			}
		case cambium.SchemaNodeKindChoice, cambium.SchemaNodeKindCase:
			// Choice/case descendants are governed by generated choice validation.
			continue
		}
	}
	return false
}

func (g *goEmitter) emitJSONParseScalarValue(varName, rawExpr, keyExpr string, f fieldInfo, out *strings.Builder, indent string) bool {
	base := fieldConcreteType(f)
	unsupported := func(reason string) bool {
		g.recordEmitError(fmt.Errorf("unsupported JSON_IETF parser for %s: %s", codegenNodePath(f.node), reason))
		return false
	}
	if f.isUnion {
		return g.emitJSONParseUnionValue(varName, rawExpr, keyExpr, f, out, indent)
	}
	if f.isIdentityref || f.isEnum {
		fmt.Fprintf(out, "%ss, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		out.WriteString(indent + "if err != nil { return err }\n")
		fmt.Fprintf(out, "%s%s, ok := Parse%s(s)\n", indent, varName, base)
		fmt.Fprintf(out, "%sif !ok { return fmt.Errorf(\"%%s has invalid identity or enum value %%q\", %s, s) }\n", indent, keyExpr)
		return true
	}
	if f.isBits {
		fmt.Fprintf(out, "%ss, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		out.WriteString(indent + "if err != nil { return err }\n")
		fmt.Fprintf(out, "%sbitNames, err := cambiumBitsNames(s)\n", indent)
		out.WriteString(indent + "if err != nil { return err }\n")
		fmt.Fprintf(out, "%s%s, err := New%s(bitNames)\n", indent, varName, base)
		out.WriteString(indent + "if err != nil { return err }\n")
		return true
	}
	switch f.jsonKind {
	case "Empty":
		fmt.Fprintf(out, "%sif err := cambiumJSONEmpty(%s, %s); err != nil { return err }\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s%s := struct{}{}\n", indent, varName)
		return true
	case "InstanceIdentifier":
		fmt.Fprintf(out, "%ss, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		out.WriteString(indent + "if err != nil { return err }\n")
		fmt.Fprintf(out, "%s%s := NewInstanceIdentifier(s)\n", indent, varName)
		return true
	case "String":
		if info, ok := binaryTypeInfo(f.node); ok {
			g.helpers["binaryParse"] = true
			fmt.Fprintf(out, "%s%s, err := cambiumJSONBinary(%s, %s, %s)\n", indent, varName, rawExpr, keyExpr, binaryLengthRangeLiteral(info))
			out.WriteString(indent + "if err != nil { return err }\n")
			return true
		}
		fmt.Fprintf(out, "%ss, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		out.WriteString(indent + "if err != nil { return err }\n")
		if f.isNewtype {
			fmt.Fprintf(out, "%s%s, err := New%s(s)\n", indent, varName, base)
			out.WriteString(indent + "if err != nil { return err }\n")
		} else if base == "string" {
			fmt.Fprintf(out, "%s%s := s\n", indent, varName)
		} else {
			fmt.Fprintf(out, "%s%s := %s(s)\n", indent, varName, base)
		}
		if info, ok := stringPatternInfo(f.node); ok {
			g.helpers["patternValidate"] = true
			fmt.Fprintf(out, "%sif err := cambiumValidateStringPatterns(s, %s); err != nil { return fmt.Errorf(\"%%s %%v\", %s, err) }\n", indent, stringPatternLiteral(info), keyExpr)
		}
		return true
	case "Bool":
		fmt.Fprintf(out, "%s%s, err := cambiumJSONBool(%s, %s)\n", indent, varName, rawExpr, keyExpr)
		out.WriteString(indent + "if err != nil { return err }\n")
		return true
	case "BareNumber", "QuotedNumber":
		if base == "Decimal64" {
			info, ok := decimal64TypeInfo(f.node)
			if !ok {
				return unsupported("missing decimal64 type info")
			}
			fd, ranges := decimal64ValidationArgs(info)
			fmt.Fprintf(out, "%s%s, err := cambiumJSONDecimal64(%s, %s, %d)\n", indent, varName, rawExpr, keyExpr, fd)
			out.WriteString(indent + "if err != nil { return err }\n")
			fmt.Fprintf(out, "%sif err := cambiumValidateDecimal64Value(%s, %d, %s); err != nil { return fmt.Errorf(\"%%s %%v\", %s, err) }\n", indent, varName, fd, ranges, keyExpr)
			return true
		}
		quoted := f.jsonKind == "QuotedNumber"
		if f.isNewtype {
			info, ok := g.intRangeTypes[base]
			if !ok {
				return unsupported("missing integer range type info")
			}
			underlying := goTypeForIntKind(info.kind)
			if isSignedIntKind(info.kind) {
				fmt.Fprintf(out, "%sparsed, err := cambiumJSONInt(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intKindBitSize(info.kind), quoted)
				out.WriteString(indent + "if err != nil { return err }\n")
				fmt.Fprintf(out, "%s%s, err := New%s(%s(parsed))\n", indent, varName, base, underlying)
			} else {
				fmt.Fprintf(out, "%sparsed, err := cambiumJSONUint(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intKindBitSize(info.kind), quoted)
				out.WriteString(indent + "if err != nil { return err }\n")
				fmt.Fprintf(out, "%s%s, err := New%s(%s(parsed))\n", indent, varName, base, underlying)
			}
			out.WriteString(indent + "if err != nil { return err }\n")
			return true
		}
		if f.intSigned {
			fmt.Fprintf(out, "%sparsed, err := cambiumJSONInt(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intBitSizeForGoType(base), quoted)
			out.WriteString(indent + "if err != nil { return err }\n")
			fmt.Fprintf(out, "%s%s := %s(parsed)\n", indent, varName, base)
		} else {
			fmt.Fprintf(out, "%sparsed, err := cambiumJSONUint(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intBitSizeForGoType(base), quoted)
			out.WriteString(indent + "if err != nil { return err }\n")
			fmt.Fprintf(out, "%s%s := %s(parsed)\n", indent, varName, base)
		}
		return true
	default:
		return unsupported("unknown JSON kind " + strconv.Quote(f.jsonKind))
	}
}

func (g *goEmitter) emitJSONParseUnionValue(varName, rawExpr, keyExpr string, f fieldInfo, out *strings.Builder, indent string) bool {
	info, ok := f.node.LeafType()
	if !ok {
		g.recordEmitError(fmt.Errorf("unsupported JSON_IETF parser for %s: missing union type info", codegenNodePath(f.node)))
		return false
	}
	union, ok := resolvedUnionType(info)
	if !ok {
		g.recordEmitError(fmt.Errorf("unsupported JSON_IETF parser for %s: unresolved union type", codegenNodePath(f.node)))
		return false
	}
	unionName := fieldConcreteType(f)
	fmt.Fprintf(out, "%svar %s %s\n", indent, varName, unionName)
	out.WriteString(indent + "matched := false\n")
	used := make(map[string]bool)
	for _, member := range union.Members() {
		variant := safeVariantIdent(unionMemberLabel(member), used)
		variantType := unionName + variant
		payloadType := unionPayloadTypeName(unionName, variant, member)
		if !g.emitJSONParseUnionMemberAttempt(varName, "matched", variantType, payloadType, rawExpr, keyExpr, member, out, indent) {
			g.recordEmitError(fmt.Errorf("unsupported JSON_IETF parser for %s: unsupported union member", codegenNodePath(f.node)))
			return false
		}
	}
	fmt.Fprintf(out, "%sif !matched { return fmt.Errorf(\"%%s does not match any generated union member\", %s) }\n", indent, keyExpr)
	return true
}

func codegenNodePath(node cambium.SchemaNodeRef) string {
	if path := node.Path(); path != "" {
		return path
	}
	return "<unknown>"
}

func resolvedUnionType(info cambium.TypeInfo) (cambium.ResolvedUnion, bool) {
	switch r := info.Resolved().(type) {
	case cambium.ResolvedUnion:
		return r, true
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return resolvedUnionType(*realtype)
		}
	}
	return cambium.ResolvedUnion{}, false
}

func (g *goEmitter) emitJSONParseUnionMemberAttempt(targetVar, matchedVar, variantType, payloadType, rawExpr, keyExpr string, member cambium.TypeInfo, out *strings.Builder, indent string) bool {
	switch r := member.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return g.emitJSONParseUnionMemberAttempt(targetVar, matchedVar, variantType, payloadType, rawExpr, keyExpr, *realtype, out, indent)
		}
		g.emitUnionStringAttempt(targetVar, matchedVar, variantType, rawExpr, keyExpr, "candidate", false, member, out, indent)
		return true
	case cambium.ResolvedString:
		patterns := stringPatternLiteral(member)
		if patterns != "nil" {
			g.helpers["patternValidate"] = true
		}
		if len(r.Length) > 0 {
			fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
			fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
			if patterns != "nil" {
				fmt.Fprintf(out, "%s\tif err == nil { if candidate, err := New%s(s); err == nil { if err := cambiumValidateStringPatterns(s, %s); err == nil { %s = %s; %s = true } } }\n", indent, payloadType, patterns, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			} else {
				fmt.Fprintf(out, "%s\tif err == nil { if candidate, err := New%s(s); err == nil { %s = %s; %s = true } }\n", indent, payloadType, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			}
			out.WriteString(indent + "}\n")
			return true
		}
		if patterns != "nil" {
			fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
			fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
			fmt.Fprintf(out, "%s\tif err == nil { if err := cambiumValidateStringPatterns(s, %s); err == nil { candidate := s; %s = %s; %s = true } }\n", indent, patterns, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			out.WriteString(indent + "}\n")
			return true
		}
		g.emitUnionStringAttempt(targetVar, matchedVar, variantType, rawExpr, keyExpr, "candidate", false, member, out, indent)
		return true
	case cambium.ResolvedUnknown:
		g.emitUnionStringAttempt(targetVar, matchedVar, variantType, rawExpr, keyExpr, "candidate", false, member, out, indent)
		return true
	case cambium.ResolvedBinary:
		g.helpers["binaryParse"] = true
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\tcandidate, err := cambiumJSONBinary(%s, %s, %s)\n", indent, rawExpr, keyExpr, binaryLengthRangeLiteral(member))
		fmt.Fprintf(out, "%s\tif err == nil { %s = %s; %s = true }\n", indent, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedBoolean:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\tcandidate, err := cambiumJSONBool(%s, %s)\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s\tif err == nil { %s = %s; %s = true }\n", indent, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedEmpty:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\terr := cambiumJSONEmpty(%s, %s)\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s\tif err == nil { candidate := struct{}{}; %s = %s; %s = true }\n", indent, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedInt:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		quoted := jsonIntegerKindQuoted(r.Kind)
		if isSignedIntKind(r.Kind) {
			fmt.Fprintf(out, "%s\tparsed, err := cambiumJSONInt(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intKindBitSize(r.Kind), quoted)
			if len(r.Range) > 0 {
				fmt.Fprintf(out, "%s\tif err == nil { if candidate, err := New%s(%s(parsed)); err == nil { %s = %s; %s = true } }\n", indent, payloadType, goTypeForIntKind(r.Kind), targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			} else {
				fmt.Fprintf(out, "%s\tif err == nil { candidate := %s(parsed); %s = %s; %s = true }\n", indent, goTypeForIntKind(r.Kind), targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			}
		} else {
			fmt.Fprintf(out, "%s\tparsed, err := cambiumJSONUint(%s, %s, %d, %t)\n", indent, rawExpr, keyExpr, intKindBitSize(r.Kind), quoted)
			if len(r.Range) > 0 {
				fmt.Fprintf(out, "%s\tif err == nil { if candidate, err := New%s(%s(parsed)); err == nil { %s = %s; %s = true } }\n", indent, payloadType, goTypeForIntKind(r.Kind), targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			} else {
				fmt.Fprintf(out, "%s\tif err == nil { candidate := %s(parsed); %s = %s; %s = true }\n", indent, goTypeForIntKind(r.Kind), targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
			}
		}
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedDecimal64:
		fd, ranges := decimal64ValidationArgs(member)
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\tcandidate, err := cambiumJSONDecimal64(%s, %s, %d)\n", indent, rawExpr, keyExpr, fd)
		fmt.Fprintf(out, "%s\tif err == nil { if err := cambiumValidateDecimal64Value(candidate, %d, %s); err == nil { %s = %s; %s = true } }\n", indent, fd, ranges, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedEnumeration:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s\tif err == nil { if candidate, ok := Parse%s(s); ok { %s = %s; %s = true } }\n", indent, payloadType, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedBits:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s\tif err == nil { if bitNames, err := cambiumBitsNames(s); err == nil { if candidate, err := New%s(bitNames); err == nil { %s = %s; %s = true } } }\n", indent, payloadType, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedIdentityRef:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
		fmt.Fprintf(out, "%s\tif err == nil { if candidate, ok := Parse%s(s); ok { %s = %s; %s = true } }\n", indent, payloadType, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	case cambium.ResolvedInstanceIdentifier:
		g.emitUnionStringAttempt(targetVar, matchedVar, variantType, rawExpr, keyExpr, "candidate", true, member, out, indent)
		return true
	case cambium.ResolvedUnion:
		fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
		fmt.Fprintf(out, "%s\tvar nestedValue %s\n", indent, payloadType)
		out.WriteString(indent + "\tnestedMatched := false\n")
		used := make(map[string]bool)
		for _, nested := range r.Members() {
			variant := safeVariantIdent(unionMemberLabel(nested), used)
			nestedVariantType := payloadType + variant
			nestedPayloadType := unionPayloadTypeName(payloadType, variant, nested)
			g.emitJSONParseUnionMemberAttempt("nestedValue", "nestedMatched", nestedVariantType, nestedPayloadType, rawExpr, keyExpr, nested, out, indent+"\t")
		}
		fmt.Fprintf(out, "%s\tif nestedMatched { candidate := nestedValue; %s = %s; %s = true }\n", indent, targetVar, unionVariantAssignExpr(variantType, member, "candidate"), matchedVar)
		out.WriteString(indent + "}\n")
		return true
	default:
		return false
	}
}

func (g *goEmitter) emitUnionStringAttempt(targetVar, matchedVar, variantType, rawExpr, keyExpr, candidate string, instanceIdentifier bool, member cambium.TypeInfo, out *strings.Builder, indent string) {
	fmt.Fprintf(out, "%sif !%s {\n", indent, matchedVar)
	fmt.Fprintf(out, "%s\ts, err := cambiumJSONString(%s, %s)\n", indent, rawExpr, keyExpr)
	if instanceIdentifier {
		fmt.Fprintf(out, "%s\tif err == nil { %s := NewInstanceIdentifier(s); %s = %s; %s = true }\n", indent, candidate, targetVar, unionVariantAssignExpr(variantType, member, candidate), matchedVar)
	} else {
		fmt.Fprintf(out, "%s\tif err == nil { %s := s; %s = %s; %s = true }\n", indent, candidate, targetVar, unionVariantAssignExpr(variantType, member, candidate), matchedVar)
	}
	out.WriteString(indent + "}\n")
}

func unionPayloadTypeName(unionName, variant string, member cambium.TypeInfo) string {
	switch r := member.Resolved().(type) {
	case cambium.ResolvedLeafRef:
		if realtype, ok := r.Realtype(); ok {
			return unionPayloadTypeName(unionName, variant, *realtype)
		}
		return "string"
	case cambium.ResolvedBoolean:
		return "bool"
	case cambium.ResolvedInt:
		if len(r.Range) > 0 {
			return unionIntRangePayloadTypeName(unionName, variant)
		}
		return goTypeForIntKind(r.Kind)
	case cambium.ResolvedDecimal64:
		return "Decimal64"
	case cambium.ResolvedString:
		if len(r.Length) > 0 {
			return unionStringLengthPayloadTypeName(unionName, variant)
		}
		return "string"
	case cambium.ResolvedBinary, cambium.ResolvedUnknown:
		return "string"
	case cambium.ResolvedEmpty:
		return "struct{}"
	case cambium.ResolvedEnumeration:
		return unionName + variant + "Enum"
	case cambium.ResolvedBits:
		return unionName + variant + "Bits"
	case cambium.ResolvedIdentityRef:
		return unionName + variant + "Enum"
	case cambium.ResolvedInstanceIdentifier:
		return "InstanceIdentifier"
	case cambium.ResolvedUnion:
		return unionName + variant + "Union"
	default:
		return "string"
	}
}

func unionIntRangePayloadTypeName(unionName, variant string) string {
	return unionName + variant + "Range"
}

func unionStringLengthPayloadTypeName(unionName, variant string) string {
	return unionName + variant + "Length"
}

func unionVariantAssignExpr(variantType string, member cambium.TypeInfo, candidate string) string {
	if unionMemberNeedsWrapper(member) {
		return fmt.Sprintf("%s{Value: %s}", variantType, candidate)
	}
	return fmt.Sprintf("%s(%s)", variantType, candidate)
}

