func decimal64ValidationArgs(info cambium.TypeInfo) (uint8, string) {
	r, ok := info.Resolved().(cambium.ResolvedDecimal64)
	if !ok {
		return 0, "nil"
	}
	fd := r.FractionDigits().Value()
	if len(r.Range) == 0 {
		return fd, "nil"
	}
	parts := make([]string, 0, len(r.Range))
	for _, bound := range r.Range {
		parts = append(parts, fmt.Sprintf("{min: %d, max: %d}", parseDecimal64BoundRaw(bound.Min(), fd), parseDecimal64BoundRaw(bound.Max(), fd)))
	}
	return fd, "[]cambiumDecimal64Range{" + strings.Join(parts, ", ") + "}"
}

func parseDecimal64BoundRaw(bound string, fractionDigits uint8) int64 {
	switch bound {
	case "min":
		return -1 << 63
	case "max":
		return 1<<63 - 1
	}
	neg := strings.HasPrefix(bound, "-")
	text := strings.TrimPrefix(bound, "-")
	wholeText, fracText, ok := strings.Cut(text, ".")
	if !ok {
		fracText = ""
	}
	for len(fracText) < int(fractionDigits) {
		fracText += "0"
	}
	if len(fracText) > int(fractionDigits) {
		fracText = fracText[:fractionDigits]
	}
	if wholeText == "" {
		wholeText = "0"
	}
	whole := new(big.Int)
	whole.SetString(wholeText, 10)
	scale := new(big.Int).Exp(big.NewInt(10), big.NewInt(int64(fractionDigits)), nil)
	raw := new(big.Int).Mul(whole, scale)
	if fracText != "" {
		frac := new(big.Int)
		frac.SetString(fracText, 10)
		raw.Add(raw, frac)
	}
	if neg {
		raw.Neg(raw)
	}
	if raw.IsInt64() {
		return raw.Int64()
	}
	if neg {
		return -1 << 63
	}
	return 1<<63 - 1
}

