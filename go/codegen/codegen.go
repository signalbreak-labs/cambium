func toPascalCase(s string) string {
	out := strings.Builder{}
	upper := true
	for _, c := range s {
		switch c {
		case '-', '_', '.':
			upper = true
		default:
			if upper {
				out.WriteRune(toUpper(c))
				upper = false
			} else {
				out.WriteRune(c)
			}
		}
	}
	if out.Len() == 0 {
		out.WriteString("Node")
	}
	return out.String()
}

func toUpper(r rune) rune {
	if r >= 'a' && r <= 'z' {
		return r - 'a' + 'A'
	}
	return r
}

