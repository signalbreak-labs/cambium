func safeFieldIdent(name string, used map[string]bool) string {
	ident := toPascalCase(name)
	if ident == "" {
		ident = "Field"
	}
	if first := []rune(ident)[0]; first >= '0' && first <= '9' {
		ident = "Field" + ident
	}
	if isGoKeyword(name) || isGeneratedFieldReservedIdent(ident) {
		ident += "_"
	}
	base := ident
	suffix := 2
	for used[ident] {
		ident = fmt.Sprintf("%s%d", base, suffix)
		suffix++
	}
	used[ident] = true
	return ident
}

